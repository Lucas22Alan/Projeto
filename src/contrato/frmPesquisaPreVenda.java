    public void pesquisa(String camp){
        tblReg.requestFocus();
        String sqlpesq=null;
        if (clsConfig.localizaporcr.equals("S")){
            sqlpesq="select tv.id_prevenda, tp.razao_social, tv.total, tv.data_lancamento,tp.cr from tpre_venda tv\n" +
                        "join tparceiros tp on tv.idcliente=tp.idparceiro where tp.razao_social like '%"+camp+"%' and tv.estado='1';";
        
        } else{
            sqlpesq="select tv.id_prevenda, tp.razao_social, tv.total, tv.data_lancamento,tp.idparceiro from tpre_venda tv\n" +
                        "join tparceiros tp on tv.idcliente=tp.idparceiro where tp.razao_social like '%"+camp+"%' and tv.estado='1';";
        
        }
        try {
               PreparedStatement pspq= conexao.getPreparedStatement(sqlpesq);
               ResultSet rspq = pspq.executeQuery();
               DefaultTableModel tb= (DefaultTableModel) tblReg.getModel();
               tb.setNumRows(0);
               while (rspq.next()){
                   tb.addRow(new Object[]{
                       rspq.getString(1),
                       rspq.getString(2)+"-"+rspq.getString(5),
                       clsaux.formato(rspq.getDouble(3)),
                       rspq.getString(4)
                   });
               }
               rspq.close();
               pspq.close();
         } catch (SQLException ex) {
            Logger.getLogger(frmPesquisaPreVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
