    public void gerarTitulos(String idcontrato){
      try {
            String sql="select tc.id_parceiro,\n" +
                        "        tc.valor,\n" +
                        "        tc.dia_vencimento,\n" +
                        "        tc.quantidade_messes_filiacao,\n" +
                        "        tc.primeira_parcela\n" +
                        " from tcontrato tc\n" +
                        " where tc.id_contrato='"+idcontrato+"';";
            PreparedStatement ps = conexao.getPreparedStatementResult(sql);
            ResultSet rs;
            rs = ps.executeQuery();
            rs.first();
            String idparc,valor=null;
            int diavenc,qntmes,i=0;
            Date priparc=null;
            idparc=rs.getString(1);
            valor=rs.getString(2);
            diavenc=rs.getInt(3);
            qntmes=rs.getInt(4);
            priparc=rs.getDate(5);
            Calendar cd= Calendar.getInstance();
            SimpleDateFormat ft = new SimpleDateFormat("dd/MM/yyyy");
            CadContasRecPagar rp= new CadContasRecPagar();
            ContasReceberDAO crDAO = new ContasReceberDAO();
            rp.setParceiro(idparc);
            rp.setValor(valor);
            rp.setEmisao(clsaux.retornaData(ft.format(cd.getTime())));
            rp.setTipo_titulo("1");
            rp.setConta("1");
            rp.setHistorico("4");
            rp.setTipo("2");
            rp.setIdcontrato(idcontrato);
            cd.setTime(priparc);
            for (i=1; i<=qntmes; i++){
                rp.setVencimento(clsaux.retornaData(ft.format(cd.getTime())));
                crDAO.inserir(rp);
                cd.add(Calendar.MONTH, 1);
                cd.set(Calendar.DAY_OF_MONTH,diavenc);
            }
            String nome,id=null;
            int numero= tblParcelas.getRowCount();
             JOptionPane.showMessageDialog(null, numero);
            if (numero!=0){
            nome= tblParcelas.getValueAt(0, 2).toString();
            id=listaInfAdcDAO.pegaidfinalizadora(nome);
            rp.setParceiro(idparc);
            cd=Calendar.getInstance();
            rp.setEmisao(clsaux.retornaData(ft.format(cd.getTime())));
            rp.setTipo("1");
            rp.setConta("1");
            rp.setHistorico("5");
            rp.setTipo("2");
            rp.setIdcontrato(idcontrato);
            rp.setTipo_pag(id);
            for(int c=0; c<numero; c++){
                rp.setVencimento(clsaux.retornaData(tblParcelas.getValueAt(c, 0).toString()));
                rp.setValor(tblParcelas.getValueAt(c, 1).toString().replace(",", "."));
                crDAO.inserir(rp);
            }
        } 
      }
        catch (SQLException ex) {
            Logger.getLogger(CRUDcontrato.class.getName()).log(Level.SEVERE, null, ex);
        }
      
      JOptionPane.showMessageDialog(null, "Titulos Gerado com Sucesso :)");
    }
