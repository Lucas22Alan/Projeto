    public void divisaoPlano(){
        int par= Integer.parseInt(CbParcelas.getSelectedItem().toString());
        Double valorplano=Double.parseDouble(txtValorplano.getText().replace(",", "."));
        Double valorparcela= valorplano/par;
        String aa = this.formato(valorparcela);
        String nmparcela=null;
        nmparcela=CbFinalizadora.getSelectedItem().toString();
        DefaultTableModel tbl = (DefaultTableModel) tblParcelas.getModel();
        tbl.setNumRows(0);
        Calendar cl = Calendar.getInstance();
        SimpleDateFormat ft = new SimpleDateFormat("dd/MM/yyyy");
        for (int i=0; i<par; i++){
            tbl.addRow(new Object[]{
                ft.format(cl.getTime()),
                aa,
                nmparcela
            });
        }
    }
