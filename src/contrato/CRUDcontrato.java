    public void pesquisaContatos(){
        String dataini,datafim=null;
        dataini=jftDataini.getText();
        datafim=jftDatafim.getText();
        if (dataini.equals("  /  /    ")||datafim.equals("  /  /   ")){
            JOptionPane.showMessageDialog(null, "Falta preencher a data !!!");
        } else {
            dataini=dataini.replaceAll("/", ".");
            datafim=datafim.replaceAll("/", ".");
        try{
        String sql="select  tp.razao_social,\n" +
                    "        tc.data,\n" +
                    "        tc.valor,\n" +
                    "        test.descricao,\n" +
                    "        tc.id_contrato\n" +
                    " from tcontrato tc\n" +
                    " join tparceiros tp on tc.id_parceiro=tp.idparceiro\n" +
                    " join testado_operacoes test on test.id=tc.estado"
                + "    where tc.data between '"+dataini+"'and'"+datafim+"'";
        PreparedStatement ps= conexao.getPreparedStatement(sql);
        ResultSet rs = ps.executeQuery();
        DefaultTableModel tb = (DefaultTableModel) tblContrato.getModel();
        tb.setNumRows(0);
        while (rs.next()){
            tb.addRow(new Object[]{
                rs.getString(5),
                rs.getString(1),
                clsaux.convertDataExib(rs.getString(2)),
                clsaux.formato(rs.getDouble(3)),
                rs.getString(4)
               
            });
         }
        rs.close();
        ps.close();
        }catch (SQLException er){
            JOptionPane.showMessageDialog(null, "Erro ao buscar dados do contrato \n" +er.getMessage());
          }
    
       }
    }
