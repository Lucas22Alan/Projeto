    public void carregaBox(){
        DefaultComboBoxModel cbm= new DefaultComboBoxModel(listaplanosDAO.listaAtiradores("0").toArray());
        cbSocio.setModel(cbm);
    }
    public void retornaCpf(){
        try {
             idcli=clsaux.retornaId(cbSocio.getSelectedItem().toString());
             String sqlbusca="select cnpj,cr from tparceiros where idparceiro='"+idcli+"'";
             PreparedStatement ps = conexao.getPreparedStatementResult(sqlbusca);
             ResultSet rs = ps.executeQuery();
             rs.first();
             txtCpf.setText(rs.getString(1));
             txtCr.setText(rs.getString(2));
             rs.close();
             ps.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "erro para bscar dados dos atiradores \n\n"+ex.getMessage());
            gravarLog.main(ex.getMessage().toString());
            Logger.getLogger(CRC.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void gravarCr(){
        contratoDAO ct = new contratoDAO();
        ct.atualizarCr(idcli, txtCr.getText());
        this.dispose();
    }
