    public void gerarRelatorio(){
        try {
            Date dataini,datafim=null;
            dataini=clsaux.retornaData(ftInicial.getText());
            datafim=clsaux.retornaData(ftFinal.getText());
            String tipofiltro=clsaux.retornaId(cbTipo.getSelectedItem().toString());
            String complementosql="";
            JDialog viewer = new JDialog(new javax.swing.JFrame(),"Visualiza√ß√£o do Relat√≥rio", true);
            viewer.setSize(1024,720);
            viewer.setLocationRelativeTo(null);
            JasperDesign jd;
            jd = JRXmlLoader.load(".\\relatorios\\relTotaisFinalizadora.jrxml");
            JasperReport jr= JasperCompileManager.compileReport(jd);
            switch (tipofiltro){
                case "1":
                    complementosql="tm.id_tipo=1";
                    break;
                case "2":
                    complementosql="(tm.id_tipo=2 or tm.id_tipo=5)";
                    break;
                    
            }
            
            if (cbFiltro.isSelected()){
                complementosql="(tm.id_tipo=2 or tm.id_tipo=5 or tm.id_tipo=1)";
            }
            String sql="select sum(tm.total) as total,\n" +
"           tm.dat_finalizacao\n" +
"           from tmovimento tm "
                    + "where "+complementosql+" and tm.estado=2 and tm.dat_finalizacao between\n" +
"                        '"+dataini+"' and '"+datafim+"'\n" +
"                        group by 2";
           String sqltotal="select tf.descricao as nomeFinalizadora,\n" +
"                           sum(iif(ta.valor_troco is null, 0.00,ta.valor_recebido-ta.valor_troco) + iif(tp.valor is null,0.00,tp.valor-tp.troco)) as valorFinalizadora,\n" +
"                            tf.taxa_adm as taxa, \n"
                   + "sum(iif(ta.valor_troco is null, 0.00,ta.valor_recebido-ta.valor_troco) + iif(tp.valor is null,0.00,tp.valor-tp.troco))-sum(iif(ta.valor_troco is null, 0.00,ta.valor_recebido-ta.valor_troco) + "
                   + "iif(tp.valor is null,0.00,tp.valor-tp.troco))*tf.taxa_adm/100 as saldo      "
                   + "from tmovimento tm\n" +
                            "left join tpagamentos_adm ta on ta.id_mov=tm.id_mov\n" +
                            "left join tpagamentos_pdv tp on tp.id_movimento=tm.id_mov\n" +
                            "join tfinalizadora tf on ta.id_finalizadora=tf.id_finalizdora or tp.id_finalizadora=tf.id_finalizdora\n" +
                            "where "+complementosql+" and tm.estado=2 and (ta.estado=2 or tp.estado=2) and tm.dat_finalizacao between '"+dataini+"' and '"+datafim+"' group by 1,3;";
            
            System.out.println(sqltotal);
            PreparedStatement ps=conexao.getPreparedStatementResult(sqltotal);
            
            PreparedStatement pss= conexao.getPreparedStatement(sql);
            ResultSet rs = ps.executeQuery();
            //rs.first();
            Double total= 0.00;
            String finalizadora=null;
            String din = "0.00",ccredi="0.00",cdebi="0.00",prazo="0.00",ccliq="0.00",cdliq="0.00",pix="0.00";
            while(rs.next()){
                total=total+rs.getDouble(2);
                if(rs.getString(1).equals("DINHEIRO")){
                    din=clsaux.formato(rs.getDouble(2));
                } else if (rs.getString(1).equals("CARTAO CREDITO")){
                    ccredi=clsaux.formato(rs.getDouble(2));
                    ccliq=clsaux.formato(rs.getDouble(4));
                } else if (rs.getString(1).equals("CARTAO DEBITO")){
                    cdebi=clsaux.formato(rs.getDouble(2));
                    cdliq=clsaux.formato(rs.getDouble(4));
                } else if(rs.getString(1).equals("A PRAZO")){
                    prazo=clsaux.formato(rs.getDouble(2));
                } else if(rs.getString(1).equals("PIX")){
                    pix=clsaux.formato(rs.getDouble(2));
                }
            }
            rs=pss.executeQuery();
            JRResultSetDataSource jrds= new JRResultSetDataSource(rs);
            Map parametros = new HashMap();
            parametros.put("RAZAO_SOCIAL", clsDadosEmpresa.getRazao());
            parametros.put("RUA", clsDadosEmpresa.getRua());
            parametros.put("NUMERO", clsDadosEmpresa.getNumero());
            parametros.put("BAIRRO", clsDadosEmpresa.getBairro());
            parametros.put("CEP", "00000-000");
            parametros.put("CIDADE", clsDadosEmpresa.getCidade());
            parametros.put("UF", clsDadosEmpresa.getEstado());
            parametros.put("FONE", clsDadosEmpresa.getFone());
            parametros.put("nomeUsuario", "geral");
            parametros.put("pDataInicial", dataini);
            parametros.put("pDataFinal", datafim);
            parametros.put("pdinheiro", din);
            parametros.put("pcartaocredito", ccredi);
            parametros.put("pcartaodebito", cdebi);
            parametros.put("pprazo", prazo);
            parametros.put("pgeral", clsaux.formato(total));
            parametros.put("pccliq", ccliq);
            parametros.put("pcdliq", cdliq);
            parametros.put("ppix", pix);
            parametros.put("REPORT_CONNECTION", conexao.getConexaoRelatorio());
            JasperPrint impre= JasperFillManager.fillReport(jr, parametros,jrds);
            JasperViewer jv = new JasperViewer(impre, true);
            viewer.getContentPane().add(jv.getContentPane());
            viewer.setVisible(true);   
            //conexao.getConexaoRelatorio().close();
        } catch (JRException ex) {
            Logger.getLogger(relTotaisFinalizdora.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao gerar relatorio \n"+ex);
        } catch (SQLException ex) {
            Logger.getLogger(relTotaisFinalizdora.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao gerar relatorio \n"+ex);
        }
    }
