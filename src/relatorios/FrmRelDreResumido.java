    public void gerarRelatorio() throws JRException, SQLException{
        String ini,fim=null;
        ini=ftInicial.getText();
        fim=ftFinal.getText();
        String conta="0";
        String con="0";
        if(ckTodos.isSelected()==true){
            conta=">0";
            con="0";
        }else{
            conta="="+clsaux.retornaId(cbConta.getSelectedItem());
            con=clsaux.retornaId(cbConta.getSelectedItem()).trim();
        }
        String caminho=".\\relatorios\\relDreResumido.jasper";
        
            HashMap parametros= new HashMap();
            parametros.put("RAZAO_SOCIAL", clsDadosEmpresa.getRazao());
            parametros.put("RUA", clsDadosEmpresa.getRua());
            parametros.put("NUMERO", clsDadosEmpresa.getNumero());
            parametros.put("BAIRRO", clsDadosEmpresa.getBairro());
            parametros.put("CEP", "00000-000");
            parametros.put("CIDADE", clsDadosEmpresa.getCidade());
            parametros.put("UF", clsDadosEmpresa.getEstado());
            parametros.put("FONE", clsDadosEmpresa.getFone());
            parametros.put("PDataInicial", clsaux.retornaData(ini));
            parametros.put("PDataFinal", clsaux.retornaData(fim));
            parametros.put("nomeUsuario", clsaux.retornaAposId(clsConfig.usuarioLogado));
            parametros.put("REPORT_CONNECTION", conexao.getConexaoRelatorio());
            parametros.put("conta", con);
            String sqltotal="select  sum(tc.entrada) as entrada,\n" +
                            "        sum(tc.saida) as saida \n "+
                            " from tmov_conta tc\n" +
                            "left join ttipo_movimento tm on tc.tipo_mov=tm.id_tipo\n" +
                            "where tc.data between ? and ? and  tm.composicao_dre='S' and tc.estado='2' and tc.id_conta"+conta;
                            
            PreparedStatement ps1= conexao.getPreparedStatementResult(sqltotal);
            ps1.setDate(1, clsaux.retornaData(ini));
            ps1.setDate(2, clsaux.retornaData(fim));
            ResultSet rs1= ps1.executeQuery();
            rs1.first();
            parametros.put("totalEntrada", rs1.getDouble(1));
            parametros.put("totalSaida", rs1.getDouble(2));
            rs1.close();
            ps1.close();
            String sql="select iif(tm.direcao='E',sum(tc.entrada),sum(tc.saida)) as valor,\n" +
                    "        tm.descricao_movimento,\n" +
                    "        tm.direcao,tm.id_tipo as idtipo\n" +
                    " from tmov_conta tc\n" +
                    "left join ttipo_movimento tm on tc.tipo_mov=tm.id_tipo\n" +
                    "where tc.data between ? and ? and tm.composicao_dre='S' and tc.estado='2' and tc.id_conta"+conta+"\n" +
                    "group by 2,3,4";
            PreparedStatement ps= conexao.getPreparedStatement(sql);
            ps.setDate(1, clsaux.retornaData(ini));
            ps.setDate(2, clsaux.retornaData(fim));
            ResultSet rs = ps.executeQuery();
            JRResultSetDataSource datasource= new JRResultSetDataSource(rs);
            JasperPrint print=null;
            print=JasperFillManager.fillReport(caminho, parametros, datasource);
            JasperViewer view= new JasperViewer(print);
            JDialog dialog=new JDialog(new javax.swing.JFrame(),"Visualiza√ß√£o Do Relat√≥rio", true);
            dialog.setSize(900, 700);
            dialog.setLocationRelativeTo(null);
            dialog.getContentPane().add(view.getContentPane());
            dialog.setVisible(true);
    }
    
