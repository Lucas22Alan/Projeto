    public void gerarRelatorio(){
        String estado,atirador,filtro=null;
        Date dataini,datafim=null;
        dataini=clsaux.retornaData(ftInicial.getText());
        datafim= clsaux.retornaData(ftFinal.getText());
        filtro=cbFiltro.getSelectedItem().toString();
        estado= clsaux.retornaId(cbEstado.getSelectedItem().toString());
        if (filtro=="Vencimento"){
            filtro="data_vencimento";
        }else if(filtro=="Pagamento"){
            filtro="data_pagamento";
        }else if(filtro=="Emissao"){
            filtro="data_emissao";
        }
        if (estado=="4"){
            estado="4' or estado='11'";
        }
        try { 
                JDialog viewer = new JDialog(new javax.swing.JFrame(),"Visualiza√ß√£o do Relat√≥rio", true);
                viewer.setSize(1024,720);
                viewer.setLocationRelativeTo(null);
                JasperDesign jd=null;
                if (cbTotalizar.isSelected()){
                    jd = JRXmlLoader.load(".\\relatorios\\reltTitulosTotalizado.jrxml");
                }else{
                    jd = JRXmlLoader.load(".\\relatorios\\reltTitulos.jrxml");
                }
                JasperReport jr= JasperCompileManager.compileReport(jd);
                String sql="select tp.razao_social ,\n" +
                            "         tc.*\n,"
                        + "           top.descricao as SITDOC \n" +
                            "from tcontas_a_receber_pagar tc \n" +
                            " join tparceiros tp on tc.id_parceiro=tp.idparceiro \n"
                        + "   join testado_operacoes top on top.id=tc.estado\n" +
                            " where pagar_receber='2' and tc.estado='"+estado+"' and "+filtro+" between '"+dataini+"' and '"+datafim+"'; ";
                String sqltotalizado="select tp.razao_social ,\n" +
                            "         sum(tc.valor) as valor, \n"
                        + "           top.descricao as SITDOC \n" +
                            "from tcontas_a_receber_pagar tc \n" +
                            " join tparceiros tp on tc.id_parceiro=tp.idparceiro \n"
                        + "   join testado_operacoes top on top.id=tc.estado\n" +
                            " where pagar_receber='2' and (tc.estado='"+estado+"' ) and "+filtro+" between '"+dataini+"' and '"+datafim+"' group by 1,3; ";
                String sqlvalortotal="select sum(valor) as total, \n" +
                            "count (tc.id) \n" + 
                            "from tcontas_a_receber_pagar tc \n" +
                            " join tparceiros tp on tc.id_parceiro=tp.idparceiro \n"
                        + "   join testado_operacoes top on top.id=tc.estado\n" +
                            " where pagar_receber='2' and tc.estado='"+estado+"' and "+filtro+" between '"+dataini+"' and '"+datafim+"';";
                if(cbTotalizar.isSelected()){
                    sql=sqltotalizado;
                }
                PreparedStatement ps=conexao.getPreparedStatementResult(sql);
                PreparedStatement pst= conexao.getPreparedStatement(sqlvalortotal);
                ResultSet rs = pst.executeQuery();
                rs.next();
                String valorto=rs.getString(1);
                String cont=rs.getString(2);
                pst.close();
                rs=ps.executeQuery();
                JRResultSetDataSource jrds= new JRResultSetDataSource(rs);
                Map parametros = new HashMap();
                parametros.put("dat_inicio", ftInicial.getText());
                parametros.put("dat_fim", ftFinal.getText());
               parametros.put("RAZAO_SOCIAL", clsDadosEmpresa.getRazao());
            parametros.put("RUA", clsDadosEmpresa.getRua());
            parametros.put("NUMERO", clsDadosEmpresa.getNumero());
            parametros.put("BAIRRO", clsDadosEmpresa.getBairro());
            parametros.put("CEP", "00000-000");
            parametros.put("CIDADE", clsDadosEmpresa.getCidade());
            parametros.put("UF", clsDadosEmpresa.getEstado());
            parametros.put("FONE", clsDadosEmpresa.getFone());
                parametros.put("totalgeral", valorto);
                parametros.put("cont", cont);
                JasperPrint impre= JasperFillManager.fillReport(jr, parametros, jrds);
                JasperViewer jv = new JasperViewer(impre, true);
                viewer.getContentPane().add(jv.getContentPane());
                viewer.setVisible(true);
                ps.close();
           } catch (JRException ex) {
            Logger.getLogger(relCReceber.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (SQLException exx) {
            Logger.getLogger(relCReceber.class.getName()).log(Level.SEVERE, null, exx);
            JOptionPane.showMessageDialog(null, exx.getMessage());
        } catch(java.lang.Throwable e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
            
    }
