    public void relCadastros(){
        Date dtini, dtfim=null;
        String plano,sql2=null;
        plano=clsaux.retornaId(cbPlanos.getSelectedItem().toString());
        
        
        try {
       JDialog viewer = new JDialog(new javax.swing.JFrame(),"Visualização do Relatório", true);
                viewer.setSize(1024,720);
                viewer.setLocationRelativeTo(null);
                JasperDesign jd;           
               jd = JRXmlLoader.load(".\\relatorios\\relCadastro.jrxml");
               JasperReport jr= JasperCompileManager.compileReport(jd);
              
               if(plano.equals("Todos")){
                    sql2="select * from tparceiros a\n" +
                           "join tenderecos b on a.idparceiro = b.id_parceiro\n" +
                           "join tcontrato c on a.idparceiro=c.id_parceiro\n" +
                           "order by a.razao_social";
               }else{
                   sql2="select * from tparceiros a\n" +
                           "join tenderecos b on a.idparceiro = b.id_parceiro\n" +
                           "join tcontrato c on a.idparceiro=c.id_parceiro\n" +
                           "where c.tipo_plano='"+plano+"'\n" +
                           "order by a.razao_social";
               }
               PreparedStatement cad= conexao.getPreparedStatement(sql2);
               ResultSet rk = cad.executeQuery();
               //rs.first();
               JRResultSetDataSource jrds= new JRResultSetDataSource(rk);
               Map parametros = new HashMap();
               parametros.put("razao_social", clsDadosEmpresa.getRazao());
               parametros.put("cnpjemitente", clsDadosEmpresa.getCnpj());
               parametros.put("plano",plano);
               JasperPrint impre= JasperFillManager.fillReport(jr, parametros, jrds);
               JasperViewer jv = new JasperViewer(impre, true);
               viewer.getContentPane().add(jv.getContentPane());
               viewer.setVisible(true);
        
        
         } catch (JRException ex) {
            Logger.getLogger(relCReceber.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (SQLException exx) {
            Logger.getLogger(relCReceber.class.getName()).log(Level.SEVERE, null, exx);
            JOptionPane.showMessageDialog(null, exx.getMessage());
        } catch(HeadlessException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }        
    }
    
    
    
