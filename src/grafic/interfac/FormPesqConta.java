    public void pesquisaConta(){
        try {
        
        String nomepesquisar=JtxtNomePesq.getText();
        String sqlpgconta="select id_conta,nome_conta,observacao from tconta where nome_conta like'%"+nomepesquisar+"%'";
        PreparedStatement pscon= conexao.getPreparedStatement(sqlpgconta);
        ResultSet rscon=pscon.executeQuery();
        DefaultTableModel tb= (DefaultTableModel) TblCont.getModel();
        tb.setNumRows(0);
        while(rscon.next()){
            tb.addRow(new Object []{
                rscon.getString(1),
                rscon.getString(2),
                rscon.getString(3)
            });
        }
        rscon.close();
        pscon.close();
        } catch (SQLException ex) {
            Logger.getLogger(FormPesqConta.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Erro ao buscar contas" +ex.getMessage());
        }
    }
