    public void dadosParceiro(){
        try {
            String sql="select  razao_social, celular from tparceiros where idparceiro=?;";
            PreparedStatement ps = conexao.getPreparedStatementResult(sql);
            ps.setString(1, idcliente);
            ResultSet rs = ps.executeQuery();
            rs.first();
            lbNome.setText(rs.getString(1));
            lbCelular.setText(rs.getString(2));
            rs.close();
            ps.close();
        } catch (SQLException ex) {
            Logger.getLogger(frmClubePontos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    public void dadosVendas(){
        try {
            String sql="select  sum(ti.pontos),\n" +
                    "        tm.dat_finalizacao,\n" +
                    "        tm.total,\n" +
                    "        tm.id_mov\n" +
                    "        from titens ti\n" +
                    "        join tmovimento tm on ti.id_mov=tm.id_mov\n" +
                    "        where tm.dat_finalizacao between\n" +
                    "        (select data_ini_fidelidade from tconfig) and current_date and tm.estado=2 and ti.estado=2 and\n" +
                    "        (tm.id_tipo=1 or tm.id_tipo=2)  and tm.id_parceiro=?\n" +
                    "        group by 2,3,4;";
            PreparedStatement ps= conexao.getPreparedStatement(sql);
            ps.setString(1, idcliente);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel tb= (DefaultTableModel) tbVendas.getModel();
            tb.setNumRows(0);
            int total=0;
            while (rs.next()){
                total=total+rs.getInt(1);
                tb.addRow(new Object[]{
                    rs.getString(4),
                    clsaux.convertDataExib(rs.getString(2)),
                    rs.getString(1),
                    clsaux.formato(rs.getDouble(3))
                });
                lbDataCompra.setText(clsaux.convertDataExib(rs.getString(2)));
            }
            int gasto=resgateDAO.buscaPtsResgatados(idcliente);
            total=total-gasto;
            lbPontos.setText(String.valueOf(total));
            rs.close();
            ps.close();
            
        } catch (SQLException ex) {
            Logger.getLogger(frmClubePontos.class.getName()).log(Level.SEVERE, null, ex);
        }
    } 
    public void itensResgate(){
        try {
            String sql="select id,nomelongo,pontos_retira from tprodutos where pontos_retira>0;";
            PreparedStatement ps = conexao.getPreparedStatement(sql);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel tb= (DefaultTableModel) tbProdResgate.getModel();
            tb.setNumRows(0);
            while(rs.next()){
                tb.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    1,
                    new Boolean(false)                
                });
            }
            rs.close();
            ps.close();
        } catch (SQLException ex) {
            Logger.getLogger(frmClubePontos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    
    
    }
    public void calculaItensSelecionado(){
        Double valor=0.00;
        for (int i=0; i<tbProdResgate.getRowCount(); i++){
            Boolean marcado= (Boolean) tbProdResgate.getValueAt(i, 4);
            if (marcado==true){
                Double ptsvale=Double.parseDouble(tbProdResgate.getValueAt(i, 2).toString().replaceAll(",", "."));
                Double qnt=Double.parseDouble(tbProdResgate.getValueAt(i, 3).toString().replaceAll(",", "."));
                Double totalitem=ptsvale*qnt;
                valor=valor+totalitem;
            }
        }
        lbPontosTotal.setText(clsaux.formato(valor));
    }
    public void inserirMovimento(){
        lancDocumentDAO ln= new lancDocumentDAO();
        clsLancDocument cs=new clsLancDocument();
        clsLancDocCabecalho cl= new clsLancDocCabecalho();
        resgateDAO rs= new resgateDAO();
        String idmvto=lancDocumentDAO.insereRegistroBranco("6");
        int item=0;
        for (int i=0; i<tbProdResgate.getRowCount(); i++){
            if((Boolean) tbProdResgate.getValueAt(i, 4)== true){
                cs.setIdmovto(idmvto);
                cs.setIdproduto(tbProdResgate.getValueAt(i, 0).toString());
                cs.setQuantidade(tbProdResgate.getValueAt(i, 3).toString());
                cs.setAcrescimo("0.00");
                cs.setPrecovenda("0.00");
                cs.setDesconto("0.00");
                cs.setUnidade("UN");
                cs.setTotal("0.00");
                cs.setNumeroitem(String.valueOf(item));
                ln.insereItensReferenteMovto(cs);
                item++;
            }
        }
        cl.setDocument("0000");
        cl.setSerie("0");
        cl.setEmissao(clsaux.retornaData(clsaux.preencheData()));
        cl.setLancamento(clsaux.retornaData(clsaux.preencheData()));
        cl.setIdparceiro(idcliente);
        cl.setBaseicms("0.00");
        cl.setBasest("0.00");
        cl.setDesconto("0.00");
        cl.setIdmovimento(idmvto);
        cl.setOutrasdesp("0.00");
        cl.setTotal("0.00");
        cl.setTotalprod("0.00");
        cl.setValoricms("0.00");
        cl.setValorst("0.00");
        ln.atualizaRegistroMovto(cl);
        String pts=lbPontosTotal.getText().replaceAll(",", ".");
        rs.inserir(idmvto, idcliente, pts);
        this.dispose();
    }
    public void validaPontosNecessario(){
        Double ptsganhos=Double.parseDouble(lbPontos.getText().replaceAll(",", "."));
        Double ptsnecessario=Double.parseDouble(lbPontosTotal.getText().replaceAll(",", "."));
        if (ptsnecessario<=ptsganhos){
            this.inserirMovimento();
        }else {
            lbMensagem.setText("Saldo de pontos Insuficiente");
        }
        
    }
