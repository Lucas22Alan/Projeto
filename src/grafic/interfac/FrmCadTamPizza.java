    public void preencheDados(String codigobarra){
         String sqlitem="select tp.id,\n" +
                        "        tp.nomelongo,\n" +
                        "        tp.unid_venda,\n" +
                        "        tb.codigo_barras,\n" +
                        "        tb.referencia,\n" +
                        "        tc.preco_venda, tc.preco_custo\n" +
                        "        from tprodutos tp\n" +
                        "join tbarras tb on tp.id=tb.id_produto\n" +
                        "join tprecos tc on tp.id=tc.id\n" +
                        "where tb.codigo_barras=? and tp.excluido='N'";
        PreparedStatement psp= conexao.getPreparedStatementResult(sqlitem);
        try {
            psp.setString(1, codigobarra);
            ResultSet rsp= psp.executeQuery();
            rsp.first();
            txtDescBase.setText(rsp.getString(2));
            txtVlVenda.setText(rsp.getString(6));
            rsp.close();
            psp.close();
        } catch (SQLException ex) {
            Logger.getLogger(LancamentoItemDocumento.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Produto Não Encontrado!!!");
        }
    }
    
    public void acaoBtnGravar(){
        cad.setBarrasorig(txtCodProdBase.getText());
        cad.setDescricao(txtDescAdicional.getText());
        cad.setPreco(clsaux.capturaValores(txtVlVenda.getText()));
        cad.setQntsabor(txtQntSabor.getText());
        cad.setQntpedaco(txtQntPedaco.getText());
        cad.setDescorig(txtDescBase.getText());
        if(new CadPizzaDAO().inserirTamanho(cad)){
            JOptionPane.showMessageDialog(null, "Gravado Com Sucesso");
            CRUDCadPizza.jLabel3.requestFocus();
        }
        this.dispose();
        
    }
    
