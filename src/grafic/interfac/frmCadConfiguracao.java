    public void carregaPermissaoGrupo(){
       String idgrupo= clsaux.retornaId(cbGrupo.getSelectedItem().toString());
        DefaultTableModel tb = (DefaultTableModel) tbPermisao.getModel();
        tb.setNumRows(0);
        for(String perm : dao.buscarPermissoesPorGrupo(idgrupo)){
            tb.addRow(new Object[]{
                clsaux.retornaId(perm),
                clsaux.retornaAposId(perm)
            });
        }
    }
    public void removerPermissaoGrupo(){
        String permissao= tbPermisao.getValueAt(tbPermisao.getSelectedRow(), 0).toString();
        String gpusuario=clsaux.retornaId(cbGrupo.getSelectedItem().toString());
        dao.removerPermissao(permissao, gpusuario);
        this.carregaPermissaoGrupo();
    }
    public void inserirPermissao(){
        String permissao= clsaux.retornaId(cbPermissao.getSelectedItem().toString());
        String gpusuario=clsaux.retornaId(cbGrupo.getSelectedItem().toString());
        String descricao=clsaux.retornaAposId(cbPermissao.getSelectedItem().toString());
        dao.inserirPermissao(permissao, gpusuario,descricao);
        this.carregaPermissaoGrupo();
    }
    public void inserirTodas(){
        int tamanho=cbPermissao.getItemCount();
        for(int i=0;i<tamanho;i++){
            String permissao= clsaux.retornaId(cbPermissao.getItemAt(i));
            String gpusuario=clsaux.retornaId(cbGrupo.getSelectedItem().toString());
            String descricao=clsaux.retornaAposId(cbPermissao.getItemAt(i));
            dao.inserirPermissao(permissao, gpusuario,descricao);
        }
        this.carregaPermissaoGrupo();
    }
    public void removerTodas(){
        int tamanho=tbPermisao.getRowCount();
        for(int i=0; i< tamanho; i++){
            String permissao= tbPermisao.getValueAt(i, 0).toString();
            String gpusuario=clsaux.retornaId(cbGrupo.getSelectedItem().toString());
            dao.removerPermissao(permissao, gpusuario);
        }
         this.carregaPermissaoGrupo();
    }
