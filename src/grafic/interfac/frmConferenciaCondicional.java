    public void grava(){
        lancamentoTiroDAO lt= new lancamentoTiroDAO();
        String idmovimento=lt.inserirMovto("0", "0.00", clsaux.preencheData());
        int indice=1;
        for(int i=0; i<tbItens.getRowCount(); i++){
            clsLancamentoTiro ct= new clsLancamentoTiro();
            ct.setIdpro(tbItens.getValueAt(i, 7).toString());
            ct.setIdmovto(idmovimento);
            ct.setNum_item(String.valueOf(indice));
            ct.setQnt(clsaux.capturaValor(tbItens.getValueAt(i, 2).toString()));
            ct.setVltotal(clsaux.capturaValor(tbItens.getValueAt(i, 4).toString()));
            ct.setTipoitem("C");
            ct.setVlunitario(clsaux.capturaValor(tbItens.getValueAt(i, 3).toString()));
            ct.setBarras(tbItens.getValueAt(i, 0).toString());
            lt.inserirItensmovto(ct);
            indice++;
        }
        CondicionalDAO.gravarRegistro(idParceiro, clsaux.capturaValores(lbValorTotal.getText()), idvendedor, idmovimento,ids);
        CondicionalDAO.atualizaBaixaConferencia(ids,"15");
        this.dispose();
     }
    
