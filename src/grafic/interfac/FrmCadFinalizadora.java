    public void inicia(){
        DefaultComboBoxModel cb= new DefaultComboBoxModel(listaDAO.listapagamento().toArray());
        cbFinalizadora.setModel(cb);
    
    
    }
    
    public void buscaDadosFinalizadora(){
        
        fina=con.buscarFinalizadora(clsaux.retornaId(cbFinalizadora.getSelectedItem()));
        txtid.setText(fina.getId());
        txtNome.setText(fina.getDescricao());
        txtobs.setText(fina.getObservacao());
        txtCondPadrao.setText(fina.getCondicaoPadrao());
        
        if(fina.getPdv().equals("S"))cbpdv.setSelected(true);else cbpdv.setSelected(false);
        if(fina.getRetaguarda().equals("S"))cbadm.setSelected(true);else cbadm.setSelected(false);
        if(fina.getConsfluxo().equals("S"))cbfluxo.setSelected(true);else cbfluxo.setSelected(false);
        if(fina.getTroco().equals("S"))cbtroco.setSelected(true);else cbtroco.setSelected(false);
        if(fina.getValidaCondicao().equals("S")) ckCondicao.setSelected(true); else ckCondicao.setSelected(false);
    
    }
    
    public void gravar(){
        fina.setId(txtid.getText());
        fina.setDescricao(txtNome.getText());
        fina.setObservacao(txtobs.getText());
        fina.setCondicaoPadrao(txtCondPadrao.getText());
        
            
        if(cbadm.isSelected()==true)fina.setRetaguarda("S");else fina.setRetaguarda("N");
        if(cbfluxo.isSelected()==true)fina.setConsfluxo("S");else fina.setConsfluxo("N");
        if(cbpdv.isSelected()== true) fina.setPdv("S");else fina.setPdv("N");
        if (cbtroco.isSelected()==true)fina.setTroco("S");else fina.setTroco("N");
        if(ckCondicao.isSelected()) fina.setValidaCondicao("S"); else fina.setValidaCondicao("N");
     con.inseirFinalizadora(fina);
     this.dispose();
    }
