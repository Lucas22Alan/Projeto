    public void gravar(){
        if(id.equals("0")){
            id=dao.retornaProximaId();
            conf.setIdconfig(id);
        }
        conf.setAgencia(txtagencia.getText());
        conf.setAgenciadig(txtagendiadig.getText());
        conf.setBanco(cbBanco.getSelectedItem().toString());
        conf.setBenef(txtbenef.getText());
        conf.setBenefdig(txtbenefdig.getText());
        conf.setCarteira(txtcarteira.getText());
        conf.setConvenio(txtxconv.getText());
        conf.setDescricao(txtNome.getText());
        conf.setInstrucoes(txtInstrucao.getText());
        conf.setLocais_pag(txtLocal.getText());
        dao.inserirAlteraConfig(conf);
    }
    
    public void buscarDadosEtiqueta(){
        conf=dao.retornaConfiguracaoBoleto(id);
        txtNome.setText(conf.getDescricao());
        txtagencia.setText(conf.getAgencia());
        txtagendiadig.setText(conf.getAgenciadig());
        txtbenef.setText(conf.getBenef());
        txtbenefdig.setText(conf.getBenefdig());
        txtxconv.setText(conf.getConvenio());
        txtcarteira.setText(conf.getCarteira());
        txtInstrucao.setText(conf.getInstrucoes());
        txtLocal.setText(conf.getLocais_pag());
        
        clsaux.carregaComboBox(cbBanco, conf.getBanco());
    
    }
    public void inicia(){
        DefaultComboBoxModel cbeti= new DefaultComboBoxModel(BoletoDAO.retornaListaConfigBoleto().toArray());
        cbBoletos.setModel(cbeti);
    
    }
    
