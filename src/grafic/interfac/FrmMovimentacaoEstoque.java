    public void buscarHistoricoCompleto(){
    DefaultTableModel tbent= (DefaultTableModel) tbTodos.getModel();
        tbent.setNumRows(0);
        Double vlanterior=0.00,estoque=0.00;
        for(clsMovimentoEstoque reg: lista){
            vlanterior=estoque;
            if(reg.getDirecao().equals("E")){
                estoque=estoque+reg.getQnd();
            }else if(reg.getDirecao().equals("S")){
                estoque=estoque-reg.getQnd();
            }else if (reg.getDirecao().equals("A")){
                estoque=reg.getQnd();
            }
            tbent.addRow(new Object[]{
                reg.getMovto(),
                reg.getParceiro(),
                clsaux.formato3(vlanterior),
                clsaux.formato3(reg.getQnd()),
                clsaux.formato3(estoque),
                reg.getDescMov(),
                clsaux.convertDataExib(reg.getData()),
                reg.getHora(),
                clsaux.formato3(reg.getValortotal()),
                reg.getDocumento(),
                reg.getModelo(),
                reg.getSerie()
            });
         }
        Double saldo=0.00;
        saldo=clsaux.capturaValores(lbTotalEnt.getText())-clsaux.capturaValores(lbTotalSai.getText());
        lbSaldo.setText(clsaux.formato3(saldo));
    }
    
