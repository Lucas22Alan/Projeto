    private void logarsistema(){
        String sql ="select idusuario, nome_usuario from tusuarios where (nome_usuario=? or idusuario=? ) and senha =?";
        String inf= login.getText();
        if(inf.matches("[0-9]+")){
        }else{
            inf="0";
        }
        PreparedStatement p = conexao.getPreparedStatement(sql);
        try {
            p.setString(1, login.getText());
            p.setString(2, inf);
            p.setString(3, senhaUsuario.getText());
            ResultSet re = p.executeQuery();
            if (re.next()){
             clsConfig.usuarioLogado = re.getString(1) + "-" + login.getText();
             nomeUsuarioLogado = re.getString(2);
             BtnLogar.setEnabled(false);
             this.verificaversao();
            }else {
                JOptionPane.showMessageDialog(null, "Usuario ou senha incorretos!!!");
            }
        } catch (SQLException ex) {
             Logger.getLogger(formLogarsistema.class.getName()).log(Level.SEVERE, null, ex);
             JOptionPane.showMessageDialog(null, "Erro ao buscar dados usuario\n" +ex.getMessage());
        } 
    }
