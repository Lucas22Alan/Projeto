    public void validarLicenca(){
        if(clsaux.retornaData(clsaux.calcularData(3, clsConfig.configuracaogeral.getUltima_consulta())).before(clsaux.retornaData(clsaux.preencheData()))==true){
            //valida se faz mais de 3 dias que foi a ultima consulta ao servidor, se sim vai consultar estado da licenca
            if(empresaDAO.verificaTetativas()<6){
                try {
                    ValidaLicencaServidorOnline val= new ValidaLicencaServidorOnline();
                    val.consultarLicencaValidaCnpj(clsDadosEmpresa.cnpj);
                } catch (IOException ex) {
                    Logs.gravarLog.gravaSemMensagem(ex.getMessage());
                    Logger.getLogger(formLogarsistema.class.getName()).log(Level.SEVERE, null, ex);
                    empresaDAO.increntaTentativa(empresaDAO.verificaTetativas()+1);
                }
            }else{
                if(clsConfig.validadeSistema.before(clsaux.retornaData(clsaux.preencheData()))==true){
                frmValidarSenhaSistema dialog = new frmValidarSenhaSistema(new javax.swing.JFrame(), true);
                dialog.preencheDados(clsDadosEmpresa.getCnpj());
                dialog.setLocationRelativeTo(null);
                dialog.setVisible(true);
            }
          }
        }
        
    }
