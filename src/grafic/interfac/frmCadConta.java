    public void gravarConta(){
        clsConta clc= new clsConta();
        String idconta=txtId.getText();
        clc.setId(idconta);
        clc.setContanome(txtNome.getText());
        clc.setLimite(txtLimite.getText().replaceAll(",", "."));
        clc.setObs(txtObs.getText());
        clc.setSaldo(txtSaldo.getText().replaceAll(",", "."));
        contaDAO ct= new contaDAO();
        if (idconta.trim().equals("")){
            ct.inserir(clc);
        } else {
           ct.alterar(clc);
        }
        this.dispose();
    }
    public void preencheDados(clsConta cc){
        txtId.setText(cc.getId());
        txtNome.setText(cc.getContanome());
        txtLimite.setText(cc.getLimite());
        txtSaldo.setText(cc.getSaldo());
        txtObs.setText(cc.getObs());
    }
