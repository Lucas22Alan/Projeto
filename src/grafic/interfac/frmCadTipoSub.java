    public void acaoBtnGravar(){
        tipoDAO tpDAO= new tipoDAO();
        String id = txtId.getText();
        String descricao=txtDescricao.getText();
        String tipo=clsaux.retornaId(cbTipo.getSelectedItem().toString());
        String direcao=clsaux.retornaId(cbDirecao.getSelectedItem().toString());
        String dre="N";
        if(cbDre.getSelectedItem().toString().equals("Sim")){
            dre="S";
        }
        if (tipo.trim().equals("T")){
            
            tpDAO.inserirTipo(descricao, tipo, direcao,id,dre);
        } else if (tipo.trim().equals("S")){
            String tiporef=clsaux.retornaId(cbTipReferente.getSelectedItem().toString());
            tpDAO.inserirSubTipo(descricao, tipo, direcao,tiporef,id,dre);
        }
        this.dispose();
    }
    public void carregaDados(){
        tabelasDAO dao = new tabelasDAO();
        ttipo_movimento tipo= dao.retornaTipoFinanceiro(id);
        txtId.setText(tipo.getId_tipo());
        txtDescricao.setText(tipo.getDescricao());
        clsaux.carregaComboBox(cbDirecao, tipo.getDirecao());
        clsaux.carregaComboBox(cbTipo, tipo.getCategoria());
        if(clsaux.trataCampoNuloConfig(tipo.getDre()).equals("N")){
            clsaux.carregaComboBox(cbDre, "Nao");
        }else{
            clsaux.carregaComboBox(cbDre, "Sim");
        }
        if(tipo.getCategoria().trim().equals("S")){
            clsaux.carregaComboBox(cbTipReferente, tipo.getTipo_referente());
        }
    }
    public void buscarTipoReferente(){
       String direcao=clsaux.retornaId(cbDirecao.getSelectedItem().toString());
       DefaultComboBoxModel vb = new DefaultComboBoxModel(listaDAO.listatipo(direcao).toArray());
       cbTipReferente.setModel(vb);
    }
