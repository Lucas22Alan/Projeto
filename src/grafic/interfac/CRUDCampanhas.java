    public void carregaDadosAlterar(){
        int id=Integer.parseInt(tbCampanhas.getValueAt(tbCampanhas.getSelectedRow(), 0).toString());
        campanha=dao.retornaCampanha(id);
        txtNome.setText(campanha.getNome());
        cbtipo.setSelectedItem(campanha.getTipo());
        carregaPrecoCampanha();
        jTabbedPane1.setSelectedIndex(1);
    
    }
    public void capturaDados(){
        campanha.setNome(txtNome.getText());
        campanha.setTipo(cbtipo.getSelectedItem().toString());
        for(int i=1; i<=7; i++){
             switch (i){
               case 1: campanha.setSeg(clsaux.capturaValores(tbDados.getValueAt(0, 1).toString()));
               break;
                case 2: campanha.setTer(clsaux.capturaValores(tbDados.getValueAt(1, 1).toString()));
               break;
                case 3: campanha.setQua(clsaux.capturaValores(tbDados.getValueAt(2, 1).toString()));
               break;
                case 4: campanha.setQui(clsaux.capturaValores(tbDados.getValueAt(3, 1).toString()));
               break; 
                case 5: campanha.setSex(clsaux.capturaValores(tbDados.getValueAt(4, 1).toString()));
               break; 
                case 6: campanha.setSab(clsaux.capturaValores(tbDados.getValueAt(5, 1).toString()));
               break;
                case 7: campanha.setDom(clsaux.capturaValores(tbDados.getValueAt(6, 1).toString()));
               break;
           }
        }
        campanha.setSituacao("3");
        
    }
    
    public void preparaIncluirNovo(){
        campanha= new Tcad_campanha();
        campanha.setIdcampanha(Integer.parseInt(new BaseGeralDAO().gerarIds("gen_tcad_campanha_id")));
        campanha.setDom(0.00);
        campanha.setQua(0.00);
        campanha.setQui(0.00);
        campanha.setSab(0.00);
        campanha.setSeg(0.00);
        campanha.setSex(0.00);
        campanha.setTer(0.00);
        carregaPrecoCampanha();
        jTabbedPane1.setSelectedIndex(1);
    } 
    
    
   public void carregaPrecoCampanha(){
       DefaultTableModel tb= (DefaultTableModel) tbDados.getModel();
       tb.setNumRows(0);
       String dia=null;
       String vl="0";
       for(int i=1; i<=7; i++){
           switch (i){
               case 1: dia="Segunda"; vl=clsaux.formato(campanha.getSeg());
               break;
                case 2: dia="Ter√ßa"; vl=clsaux.formato(campanha.getTer());
               break;
                case 3: dia="Quarta"; vl=clsaux.formato(campanha.getQua());
               break;
                case 4: dia="Quinta"; vl=clsaux.formato(campanha.getQui());
               break; 
                case 5: dia="Sexta"; vl=clsaux.formato(campanha.getSex());
               break; 
                case 6: dia="Sabado"; vl=clsaux.formato(campanha.getSab());
               break;
                case 7: dia="Domingo"; vl=clsaux.formato(campanha.getDom());
               break;
           }
           tb.addRow(new Object[]{
               dia,vl
           });
           
       }
   } 
    
    public void buscaDados(){
        campanhas= dao.retornaListaCampanhas();
        carregaTabela();
    }
    
    public void carregaTabela(){
        DefaultTableModel tb= (DefaultTableModel) tbCampanhas.getModel();
        tb.setNumRows(0);
        campanhas.forEach((cam) -> {
            tb.addRow(new Object[]{
                cam.getIdcampanha(),
                cam.getNome(),
                cam.getTipo()
            
            });
        });
    }
