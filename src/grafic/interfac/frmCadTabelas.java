    private void btnConfirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmaActionPerformed
        if (CRUDTabelas.tipoform.equals("G")||tipo.equals("G")||tipo.equals("SG")||tipo.equals("S")){
            if(txtId.getText().trim().equals("")){
            this.inserirGrupo();
            }else {
                this.alterarGrupo();
            }
        }else if (CRUDTabelas.tipoform.equals("HI")){
            if(txtId.getText().trim().equals("")){
                this.inserirHistorico();
            } else {
                this.alterarHistorico();
            }
        }
        CRUDTabelas.btnBuscar.requestFocus();
    }//GEN-LAST:event_btnConfirmaActionPerformed

