    public void cancelarOs(){
       int yn= JOptionPane.showConfirmDialog(null, "Deseja Cancelar a Operação?", "Cancelar Operação", JOptionPane.YES_NO_OPTION);
        if (yn== JOptionPane.YES_OPTION){
            ClsCancelamentos canc= new ClsCancelamentos();
            if(TbLancamentos.getValueAt(TbLancamentos.getSelectedRow(), 1).toString().equals("Em Andamento")){
                canc.cancelaOrdemServicoPeloMovimento(TbLancamentos.getValueAt(TbLancamentos.getSelectedRow(), 0).toString());
            }
            if(TbLancamentos.getValueAt(TbLancamentos.getSelectedRow(), 1).toString().equals("Finalizado")){
                // se pedido estiver finalizado, existe venda e movimentacao financeira, isso ira cancelar também
                String existenfe=orcamentoDAO.verificaExistenciaNfeReferenteRegistro(TbLancamentos.getValueAt(TbLancamentos.getSelectedRow(), 0).toString(), "4");
                if(existenfe.equals("0")){
                     canc.cancelarMovimentacaoReferente(orcamentoDAO.retornaIdMovimento(TbLancamentos.getValueAt(TbLancamentos.getSelectedRow(), 0).toString(), "4"));
                }
                else{
                    JOptionPane.showMessageDialog(null, "Atenção!!!\nExiste NFe Referente a Esse Lancamento\nPor Favor Cancele A NFe...");
                }
            }
            this.localizaOs();
        }
    
    }
    
