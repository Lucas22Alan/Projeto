    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome, unidade, fat="";
        Unidades unid= new Unidades();
        InfAdcDAO undao = new InfAdcDAO();
        unid.SetNome(TxtNomeUnidade.getText());
        unid.SetUnidade(TxtSigla.getText());
        String ad="1";
        fat=CbFator.getSelectedItem().toString();
        if (fat.equals(ad)){
            unid.SetFator("1");
        }else {
            unid.SetFator("2");
        }
        if(clsaux.verificaNulo(txtId.getText())==true){
             undao.Inserir(unid);
        }else{
            unid.setId(txtId.getText());
            undao.alterar(unid);
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

