    public void validaSenha(){
        String baseSenha=lbCnpj.getText().substring(0, 8);
        Double base=clsaux.capturaValores(baseSenha);
        int dias= Integer.parseInt(txtDias.getText());
        int diaMes= Integer.parseInt(clsaux.retornaDia());
        Double resultado=base/dias;
        resultado=resultado/diaMes;
        
        Double senhaInf=clsaux.capturaValores(txtsenha.getText());
        
        if(senhaInf.intValue()==resultado.intValue()){
            JOptionPane.showMessageDialog(null, "Sistema Liberado!!!");
            empresaDAO ep= new empresaDAO();
            ep.atualizaVencimentoSistema(dias);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Senha Errada!!!");
        }
     }
