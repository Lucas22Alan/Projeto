    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String conta,direcao,tipo,subtipo,idfinalizadora,entrada,saida=null;
        conta=clsaux.retornaId(JftfConta.getText());
        direcao=clsaux.retornaId(JcbDirecao.getSelectedItem().toString());
        tipo=clsaux.retornaId(JcbTipo.getSelectedItem().toString());
        subtipo=clsaux.retornaId(JcbSubTipo.getSelectedItem().toString());
        idfinalizadora=clsaux.retornaId(JcbFinaliz.getSelectedItem().toString());
        CadDocMovConta cadmc = new CadDocMovConta();
        if(direcao.equals("Entrada")){
            entrada=JtxtValor.getText().toString().replaceAll(",", ".");
            saida="0.00";
            cadmc.setDesc_mov("MOVIMENTACAO DE ENTRADA POR CADASTRO DE MOVIMENTO DE CONTA");
        }else {
            entrada="0.00";
            saida=JtxtValor.getText().toString().replaceAll(",", ".");
            cadmc.setDesc_mov("MOVIMENTACAO DE SAIDA POR CADASTRO DE MOVIMENTO DE CONTA");
        }
        MovimentoContaDAO mvDAO= new MovimentoContaDAO(); 
        cadmc.setIdconta(conta);
        cadmc.setIdparceiro(clsaux.trataCampoNulo(JtxtParceiro.getText()));
        cadmc.setN_doc(JtxtnDoc.getText());
        cadmc.setData_lancamento(clsaux.retornaData(JftfDatLanc.getText()));
        cadmc.setTipo_mov(tipo);
        cadmc.setSubtipo_mov(subtipo);
        cadmc.setEntrada(entrada);
        cadmc.setSaida(saida);
        cadmc.setData_venc("");
        cadmc.setIdfinalizadora(idfinalizadora);
        cadmc.setObservacao(JtxpObs.getText());
        cadmc.setIddocumento(id);
        if(clsaux.verificaNulo(JtxtParceiro.getText())==true)cadmc.setIdparceiro("0");
        if (tipoform.equals("I")){
            mvDAO.inserir(cadmc);
        } else if (tipoform.equals("A")){
            mvDAO.alterar(cadmc);
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed
