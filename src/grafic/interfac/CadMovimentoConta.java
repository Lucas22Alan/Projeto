    public void dadosAlteracao(){
        try {
            String sql="select tm.id_conta,\n" +
                        "       tm.data,\n" +
                        "       tm.id_parceiro,\n" +
                        "       tm.tipo_mov,\n" +
                        "       tm.subtipo_mov,\n" +
                        "       tm.entrada,\n" +
                        "       tm.saida ,\n" +
                        "       tm.observacao,\n" +
                        "       tm.tip_finalizadora,\n" +
                        "       tc.nome_conta,\n" +
                        "       tp.razao_social,\n" +
                        "       tt.descricao_movimento,\n" +
                        "       tt1.descricao_movimento,\n" +
                        "       tf.descricao\n" +
                        " from tmov_conta tm\n" +
                        "join tfinalizadora tf on tm.tip_finalizadora=tf.id_finalizdora\n" +
                        "left join tparceiros tp on tm.id_parceiro=tp.idparceiro\n" +
                        "join ttipo_movimento tt on tm.tipo_mov=tt.id_tipo\n" +
                        "join ttipo_movimento tt1 on tm.subtipo_mov=tt1.id_tipo\n" +
                        "join tconta tc on tm.id_conta =tc.id_conta where tm.id_movimentacao=?\n" +
                        "order by 1 asc";
            PreparedStatement ps = conexao.getPreparedStatementResult(sql);
            ps.setString(1, id);
            ResultSet rs = ps.executeQuery();
            rs.first();
            JftfConta.setText(rs.getString(1));
            JftfDatLanc.setText(clsaux.convertDataExib(rs.getString(2)));
            JtxtParceiro.setText(rs.getString(3));
            JtxpObs.setText(rs.getString(8));
            Double entrada=rs.getDouble(6);
            Double saida=rs.getDouble(7);
            Double valor=0.00;
            if (entrada==0){
                valor=saida;
                JcbDirecao.setSelectedIndex(1);
            } else if(saida==0){
                valor=entrada;
                JcbDirecao.setSelectedIndex(0);
            }
            JtxtValor.setText(clsaux.formato(valor));
            JcbConta.setSelectedItem(rs.getString(1)+"- "+rs.getString(10));
            LbNomeParc.setText(rs.getString(11));
            JcbTipo.setSelectedItem(rs.getString(4)+"- "+rs.getString(12));
            JcbSubTipo.setSelectedItem(rs.getString(5)+"- "+rs.getString(13));
            JcbFinaliz.setSelectedItem(rs.getString(9)+"- "+rs.getString(14));
        } catch (SQLException ex) {
            Logger.getLogger(CadMovimentoConta.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
