    public void inicia(){
        InputMap imp= this.getRootPane().getInputMap(JPanel.WHEN_IN_FOCUSED_WINDOW);
        imp.put(KeyStroke.getKeyStroke(KeyEvent.VK_F10,0),"busca");
        imp.put(KeyStroke.getKeyStroke(KeyEvent.VK_F6,0),"grava");
        this.getRootPane().setInputMap(JPanel.WHEN_IN_FOCUSED_WINDOW, imp);
        this.getRootPane().getActionMap().put("busca", new AbstractAction(){
            public void actionPerformed(ActionEvent ev){
                if(txtCnpj.isFocusOwner()==true){
                    frmCadCondicional.chamaBuscaParceiro();
                   } else {
                    frmCadCondicional.chamaBuscaProduto();
                }
            }
        });
         this.getRootPane().getActionMap().put("grava", new AbstractAction(){
            public void actionPerformed(ActionEvent ev){
               gravarRegistro();
            }
        });
        txtDataLanc.setText(clsaux.preencheData());
        DefaultComboBoxModel cbe=new DefaultComboBoxModel (ContasReceberDAO.estadoTituloParaDoc().toArray());
        cbEstado.setModel(cbe);
        DefaultComboBoxModel cbv=new DefaultComboBoxModel (ContasReceberDAO.vendedor().toArray());
        cbVendedor.setModel(cbv);
        if (tipoform.equals("I")){
            this.novoRegistro();
            txtCnpj.requestFocus();
        } else if(tipoform.equals("A")){
            this.buscaCabecalho();
            this.buscaItens();
            txtCodBarra.requestFocus();
        }
        
    }
