    public void gravarRegistro(){
        try {
            String sql="update tpre_venda tv set tv.idcliente=?,total=?,tipo='C', funcionario=?,data_entregue=?,estado=? where tv.id_prevenda=?";
            PreparedStatement ps = conexao.getPreparedStatement(sql);
            ps.setString(1, idparceiro);
            ps.setString(2, lbValorTotal.getText().replaceAll(",", "."));
            ps.setString(3, clsaux.retornaId(cbVendedor.getSelectedItem().toString()));
            ps.setDate(4, clsaux.retornaData(txtDataRetirada.getText()));
            ps.setString(5, clsaux.retornaId(cbEstado.getSelectedItem()));
            ps.setString(6, txtIdMov.getText());
            ps.executeUpdate();
            ps.close();
        } catch (SQLException ex) {
            Logger.getLogger(frmCadCondicional.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao Gravar registro\n" +ex.getMessage());
        }
        JOptionPane.showMessageDialog(null, "Registro Gravado com sucesso");
         this.dispose();
    }
    public void buscaCabecalho(){
        try {
            String sqlcabecalho="select tv.id_prevenda,\n" +
                    "        tv.data_lancamento,\n" +
                    "        tv.data_entregue,\n" +
                    "        tv.estado,\n" +
                    "        tv.funcionario ,\n" +
                    "        tp.cnpj,\n"
                    + "      tp.razao_social,   \n" +
                    "        tf.nome,\n" +
                    "        tr.descricao, \n"
                    + "      tv.total,tv.idcliente \n" +
                    "from tpre_venda tv\n" +
                    "left join tfuncionarios tf on tv.funcionario=tf.id\n" +
                    "left join tparceiros tp on tp.idparceiro= tv.idcliente\n" +
                    "join testado_operacoes tr on tv.estado=tr.id where tv.id_prevenda=?";
            PreparedStatement ps = conexao.getPreparedStatementResult(sqlcabecalho);
            ps.setString(1, this.idregistro);
            ResultSet rs = ps.executeQuery();
            rs.first();
            txtCnpj.setText(rs.getString(6));
            txtNome.setText(rs.getString(7));
            txtDataLanc.setText(clsaux.convertDataExib(rs.getString(2)));
            txtIdMov.setText(rs.getString(1));
            txtDataRetirada.setText(rs.getString(3));
            cbEstado.setSelectedItem(rs.getString(4)+"- "+rs.getString(9));
            cbVendedor.setSelectedItem(rs.getString(5)+"- "+rs.getString(8));
            valortotal=rs.getDouble(10);
            idparceiro=rs.getString(11);
            lbValorTotal.setText(clsaux.formato(valortotal));
            rs.close();
            ps.close();
        } catch (SQLException ex) {
            Logger.getLogger(frmCadCondicional.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao Buscar registro\n" +ex.getMessage());
        }
        
    }
    public void buscaItens(){
        try {
            String sqlitens="select tp.id_item,\n" +
                    "       tt.nomecurto,\n" +
                    "       tp.id_produto,\n" +
                    "       tp.qnt,\n" +
                    "       tp.valor_unitario,\n" +
                    "       tp.vl_total, tp.barras\n" +
                    "from titens_prevenda tp\n" +
                    "join tprodutos tt on tp.id_produto=tt.id\n" +
                    "where tp.id_prevenda=?";
            PreparedStatement ps = conexao.getPreparedStatement(sqlitens);
            ps.setString(1, idregistro);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel tb = (DefaultTableModel) tbProd.getModel();
            while(rs.next()){
                tb.addRow(new Object[]{
                    rs.getString(7),
                    rs.getString(2),
                    rs.getString(4),
                    clsaux.formato(rs.getDouble(5)),
                    clsaux.formato(rs.getDouble(6)),
                    rs.getString(1)
                });
                indice=rs.getInt(1);
                quantidadetotal=quantidadetotal+rs.getDouble(4);
            }
            rs.close();
            ps.close();
            lbQntTotal.setText(clsaux.formato(quantidadetotal));
        } catch (SQLException ex) {
            Logger.getLogger(frmCadCondicional.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    
    }
