     public void atulizaCategoriapreco(){
        produtoDAO pdDAO= new produtoDAO();
        String id,pr,prod,nome=null;
        prod=idproduto;
        nome=nomepro;
        int reg=tbCatpre.getRowCount();
        for (int i=0; i<reg; i++){
            id=clsaux.retornaId(tbCatpre.getValueAt(i, 0).toString());
            pr=clsaux.capturaValor(tbCatpre.getValueAt(i, 1).toString());
            pdDAO.atualizaCategoriaPreco(id, pr,prod);
        }
        JOptionPane.showMessageDialog(null, "Gravado com sucesso!!!");
        this.dispose();
    }
     public void excluirCategoriaPreco(){
        produtoDAO pd= new produtoDAO();
        String idcatcomp= tbCatpre.getValueAt(tbCatpre.getSelectedRow(),0).toString();
        String idcat=clsaux.retornaId(idcatcomp);
        pd.excluirCategoriaPreco(idcat, idproduto);
        ((DefaultTableModel) tbCatpre.getModel()).removeRow(tbCatpre.getSelectedRow());
     }
     public void pegaCategoriaPreco(String idcoditem){
        lbDescricao.setText(nomepro);
        DefaultComboBoxModel df = new DefaultComboBoxModel (listaInfAdcDAO.retornaCategorias().toArray());
        cbCategoria.setModel(df);
        String sql="select tc.id_categoria,\n" +
                    "        tp.desc_plano,\n" +
                    "        tc.prec_venda\n" +
                    " from tcat_precos tc\n" +
                    " join tplanos_clube tp on tc.id_categoria=tp.id_plano\n" +
                    " where tc.idprod=?";
        PreparedStatement ps = conexao.getPreparedStatement(sql);
        try {
            ps.setString(1, idcoditem);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel tb = (DefaultTableModel) tbCatpre.getModel();
            tb.setNumRows(0);
            while (rs.next()){
                String categoria=rs.getString(1).trim()+" -"+rs.getString(2);
                String prec=clsaux.formato(rs.getDouble(3));
                tb.addRow(new Object[]{
                    categoria,prec
                });
            }
            rs.close();
            ps.close();
        } catch (SQLException ex) {
            Logger.getLogger(cadProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
     public void inserirValorTbl(String prec){
         DefaultTableModel tb = (DefaultTableModel) tbCatpre.getModel();
         int reg= tbCatpre.getRowCount();
         String cat=cbCategoria.getSelectedItem().toString();
         System.out.println(reg);
         for (int i=0; i<reg; i++){
             System.out.println(i);
             System.out.println(tbCatpre.getValueAt(i, 0).toString());
             String tab=tbCatpre.getValueAt(i, 0).toString();
             if(cat.equals(tab)){
                 tb.removeRow(i);
             }
             reg=tbCatpre.getRowCount();
         };
         tb.addRow(new Object[]{
                     cat,prec
                 });
         
     }
