    public void capturaDados(){
        try {
            String sql="select tc.id_movimentacao,\n" +
"                            tc.descricao_movimentacao,\n" +
"                            tm.descricao_movimento,\n" +
"                            ttp.descricao_movimento,\n" +
"                            th.historico,\n" +
"                            tc.data,\n" +
"                            tc.hora,\n" +
"                            tc.saida,\n" +
"                            tc.entrada,\n" +
"                            tc.observacao,\n" +
"                            tc.id_contas_receber_pagar,\n" +
"                            p.razao_social as razao_contas\n" +
"                    from tmov_conta tc\n" +
"                    left join ttipo_movimento tm on tc.tipo_mov=tm.id_tipo\n" +
"                    left join ttipo_movimento ttp on tc.subtipo_mov=ttp.id_tipo\n" +
"                    left join thistorico th on tc.id_historico=th.id\n" +
"                    left join tcontas_a_receber_pagar tcp on tc.id_contas_receber_pagar=tcp.id\n" +
"                    left join tmovimento m on tc.id_movto=m.id_mov\n" +
"                    left join tparceiros p on tc.id_parceiro=p.idparceiro\n" +
"                    where tc.id_movimentacao=?;";
            PreparedStatement pd = conexao.getPreparedStatementResult(sql);
            pd.setString(1, idcontpag);
            ResultSet rs = pd.executeQuery();
            rs.first();
                String nomecr=rs.getString(12);
                if (nomecr==null){
                    
                         lbInfparceiro.setText("Não encontrado");
                   
                } else {
                    lbInfparceiro.setText(nomecr);
                }
                //lbInfparceiro.setText(nomecr+"("+nomemov+")");
                lbDescmov.setText(rs.getString(2));
                lbTipo.setText(rs.getString(3));
                lbSubtipo.setText(rs.getString(4));
                lbHistorico.setText(rs.getString(5));
                lbData.setText(clsaux.convertDataExib(rs.getString(6))+rs.getString(7));
                lbVentrada.setText(clsaux.formato(rs.getDouble(9)));
                lbVsaida.setText(clsaux.formato(rs.getDouble(8)));
                txtObs.setText(rs.getString(10));
                    } catch (SQLException ex) {
            Logger.getLogger(frmMostraInfMovimento.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao buscar dados do movimento \n"+ex.getMessage());
        }
    
    
    }

