    public void gravarDadosEmail(){
        mail.setEmail(txtMail.getText());
        mail.setSenha(txtSenha.getText());
        mail.setSmtp(txtServidor.getText());
        mail.setPorta(txtPorta.getText());
        if(cbTsl.getSelectedIndex()==0)mail.setTls("true"); else mail.setTls("false");
        if(cbSsl.getSelectedIndex()==0)mail.setAut("true"); else mail.setAut("false");
        new EmailDAO().inserirAtualizar(mail);
        
    }
