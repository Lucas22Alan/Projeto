    public void buscaEmail(){
        mail=EmailDAO.buscar(mail);
        txtMail.setText(mail.getEmail());
        txtSenha.setText(mail.getSenha());
        txtServidor.setText(mail.getSmtp());
        txtPorta.setText(mail.getPorta());
        if(mail.getTls().equals("true")) cbTsl.setSelectedIndex(0); else cbTsl.setSelectedIndex(1);
        if(mail.getAut().equals("true")) cbSsl.setSelectedIndex(0); else cbSsl.setSelectedIndex(1);
        
    }
    
    public void buscaCep(String cep){
          try{
        clsConsultaCepCorreio correio= ConsultaCepCorreio.consultarCep(cep);
        if(correio!=null){
            txtLogradouro.setText(correio.getLogradouro());
            txtBairro.setText(correio.getBairro());
            cbEstado.setSelectedItem(correio.getUf());
            cbCidade.setSelectedItem(correio.getLocalidade());
        }
        }catch(Exception epp){
            JOptionPane.showMessageDialog(null, epp.getMessage());
        }
    
    }
    public void localizaImagem(){
        JFileChooser escolherArquivo = new JFileChooser();
                int resultado = escolherArquivo.showOpenDialog(null);

                if (resultado == JFileChooser.APPROVE_OPTION) {
                    
                    File arquivoSelecionado = escolherArquivo.getSelectedFile();
                    carregaImagem(arquivoSelecionado);
                }
    } 
    
    public void carregaImagem(File arq){
        try {
            Image imagem = ImageIO.read(arq);
            ImageIcon imagemIcon = new ImageIcon(imagem);
            LabelLogo.setIcon(imagemIcon);
        } catch (IOException ex) {
            Logger.getLogger(FormEmpresa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
