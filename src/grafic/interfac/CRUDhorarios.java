    public void inicia(){
        tbDados.getColumnModel().getColumn(1).setCellEditor(validaDataTabela.validaHora());
        tbDados.getColumnModel().getColumn(2).setCellEditor(validaDataTabela.validaHora());
    }
    
    
    public void capturaDados(){
         DefaultTableModel tb= (DefaultTableModel) tbDados.getModel();
         for(int i=0; i<tb.getRowCount(); i++){
            Thorarios hora= new Thorarios();
            hora.setIdconf(1);
            hora.setDia(tb.getValueAt(i, 0).toString());
            hora.setAbertura(clsaux.trataCampoNulo(tb.getValueAt(i, 1)));
            hora.setFecha(clsaux.trataCampoNulo(tb.getValueAt(i, 2)));
            if(hora.getAbertura().equals("0")|| hora.getFecha().equals("0")){
                JOptionPane.showMessageDialog(null, "Hora Invalida, Registro NÃ£o Sera Atualizado!");
            }
            else {
                dao.gravarRegistro(hora);
            }
        }
    }
    
   
    
    
   
    
    public void buscaDados(){
        horarios= dao.retornaHorarios();
        carregaTabela();
    }
    
    public void carregaTabela(){
        DefaultTableModel tb= (DefaultTableModel) tbDados.getModel();
        tb.setNumRows(0);
        horarios.forEach((cam) -> {
            tb.addRow(new Object[]{
                cam.getDia(),
                cam.getAbertura(),
                cam.getFecha()
            
            });
        });
    }
