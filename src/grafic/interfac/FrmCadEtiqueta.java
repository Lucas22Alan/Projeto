    public void gravar(){
        etiquetaDAO dao= new etiquetaDAO();
        clsEtiqueta eti= new clsEtiqueta();
        
        if(id.equals("0")){
            eti.setId(dao.geraIdEtiqueta());// gera uma nova id
        }else{
            eti.setId(id);
        }
        eti.setNome(txtNome.getText());
        eti.setNomefxml(txtFxml.getText());
        eti.setImpcabecalho(txtCab.getText());
        eti.setImprodape(txtRodape.getText());
        
        if(cbBarra.isSelected()==true)eti.setImpbarra("S");else eti.setImpbarra("N");
        if(cbDesc.isSelected()==true)eti.setImpdesc("S");else eti.setImpdesc("N");
        if(cbOferta.isSelected()== true) eti.setImpprecopromocao("S");else eti.setImpprecopromocao("N");
        if (cbPrec.isSelected()==true)eti.setImppreco("S");else eti.setImppreco("N");
        
        if(dao.gravaEtiqueta(eti)){
            JOptionPane.showMessageDialog(null, "Registro Gravado Com Sucesso!!!");
            this.dispose();
        }
    
    }
    
    public void buscarDadosEtiqueta(){
        clsEtiqueta eti= new  clsEtiqueta();
        eti.setId(id);
        eti=etiquetaDAO.retornaEtiqueta(eti);
        
        txtNome.setText(eti.getNome());
        txtFxml.setText(eti.getNomefxml());
        txtCab.setText(eti.getImpcabecalho());
        txtRodape.setText(eti.getImprodape());
        
        if(eti.getImpbarra().equals("S"))cbBarra.setSelected(true);else cbBarra.setSelected(false);
        if(eti.getImpdesc().equals("S"))cbDesc.setSelected(true);else cbDesc.setSelected(false);
        if(eti.getImpprecopromocao().equals("S"))cbOferta.setSelected(true);else cbOferta.setSelected(false);
        if(eti.getImppreco().equals("S"))cbPrec.setSelected(true);else cbPrec.setSelected(false);
            
    
    }
    public void inicia(){
        DefaultComboBoxModel cbeti= new DefaultComboBoxModel(etiquetaDAO.listaEtiquetas().toArray());
        cbEtiquetas.setModel(cbeti);
    
    }
    
