    public void buscaSaida(){
        try {
            String sql="select tm.id_mov,tp.razao_social,tm.total,tm.dat_finalizacao,\n" +
                    "        tr.descricao,tm.numero_documento,tm.id_modelo,tm.serie, td.descricao\n" +
                    "    from tmovimento tm\n" +
                    "    left join tparceiros tp on tm.id_parceiro=tp.idparceiro\n" +
                    "    left join testado_operacoes tr on tm.estado=tr.id\n" +
                    "    left join ttipo_documentos td on tm.id_tipo=td.id\n" +
                    "    where tm.dat_finalizacao between ? and ? and td.direcao='S'\n" +
                    "    and tm.id_parceiro=?";
            PreparedStatement ps = conexao.getPreparedStatement(sql);
            ps.setDate(1, clsaux.retornaData(ftInicial.getText()));
            ps.setDate(2, clsaux.retornaData(ftFinal.getText()));
            ps.setString(3, parceiro);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel tbent= (DefaultTableModel) tbSaidas.getModel();
            tbent.setNumRows(0);
            while (rs.next()){
                tbent.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    clsaux.formato(rs.getDouble(3)),
                    clsaux.convertDataExib(rs.getString(4)),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7),
                    rs.getString(8),
                    rs.getString(9)
                });
            }
            rs.close();
            ps.close();
        } catch (SQLException ex) {
            Logger.getLogger(FrmMovimentacaoParceiro.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
