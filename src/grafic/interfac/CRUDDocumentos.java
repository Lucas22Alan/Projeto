    public void localizaRegistroOutraJanela(String id){
        try {
            String sql="select  td.descricao,\n" +
                    "        tp.razao_social,\n" +
                    "        tm.valor_itens,\n" +
                    "        tm.acrescimo,\n" +
                    "        tm.desconto,\n" +
                    "        tm.total,\n" +
                    "        tm.data,\n" +
                    "        tm.dat_finalizacao,\n" +
                    "        tm.id_mov,"
                    + "tm.numero_documento,"
                    + "tm.serie,tm.id_modelo\n" +
                    " from tmovimento tm\n" +
                    " join ttipo_documentos td on tm.id_tipo=td.id\n" +
                    " left join tparceiros tp on tm.id_parceiro=tp.idparceiro\n" +
                    " where tm.id_mov=? order by 9 desc";
            
            PreparedStatement ps = conexao.getPreparedStatementResult(sql);
            ps.setString(1, id);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel tb = (DefaultTableModel) TbLancamentos.getModel();
            TbLancamentos.setRowSorter( new TableRowSorter(tb));
            tb.setNumRows(0);
            if(rs.next()){
                tb.addRow(new Object[]{
                    rs.getString(9),
                    rs.getString(10),
                    rs.getString(1),
                    rs.getString(2),
                    clsaux.formato(rs.getDouble(3)),
                    clsaux.formato(rs.getDouble(4)),
                    clsaux.formato(rs.getDouble(5)),
                    clsaux.formato(rs.getDouble(6)),
                    clsaux.convertDataExib(rs.getString(7)),
                    clsaux.convertDataExib(rs.getString(8)),
                    rs.getString(11),
                    rs.getString(12)
                });
            }
            rs.close();
            ps.close();
        } catch (SQLException ex) {
            Logger.getLogger(CRUDDocumentos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    public void localizaDocumentos(){
        try {
            int totalreg=0;
            Double valortotalregistros=0.00;
            String tipodoc=clsaux.retornaId(JcbTipFiltro.getSelectedItem().toString());
            String estado=clsaux.retornaId(JcbEstado.getSelectedItem().toString());
            Date dataini=clsaux.retornaData(JftDatInicio.getText());
            Date datafim =clsaux.retornaData(JftDatFim.getText());
            String tipodata=JcbCont.getSelectedItem().toString();
            String campo=txtNome.getText();
            if (tipodata.equals("Emissão")){
                tipodata="data";
            } else if (tipodata.equals("Lançamento")){
                tipodata="dat_finalizacao";
            }
            if (tipodoc.equals("2")){
                tipodoc=tipodoc+" or tm.id_tipo=5";
            }
            String sql="select  td.descricao,\n" +
                    "        tp.razao_social,\n" +
                    "        tm.valor_itens,\n" +
                    "        tm.acrescimo,\n" +
                    "        tm.desconto,\n" +
                    "        tm.total,\n" +
                    "        tm.data,\n" +
                    "        tm.dat_finalizacao,\n" +
                    "        tm.id_mov,"
                    + "tm.numero_documento,"
                    + "tm.serie,tm.id_modelo, tm.hora_finalizacao\n" +
                    " from tmovimento tm\n" +
                    " join ttipo_documentos td on tm.id_tipo=td.id\n" +
                    " left join tparceiros tp on tm.id_parceiro=tp.idparceiro\n" +
                    " where (tm.id_tipo="+tipodoc+" )and tm."+tipodata+" between '"+dataini+"' and '"+datafim+"' and tm.estado=? \n"
                    +"and (tp.razao_social like'%"+campo+"%' or tm.numero_documento like '%"+campo+"%' )order by 9 desc";
            if(tipodoc.equals("99")){
                sql="select  td.descricao,\n" +
                    "        tp.razao_social,\n" +
                    "        tm.valor_itens,\n" +
                    "        tm.acrescimo,\n" +
                    "        tm.desconto,\n" +
                    "        tm.total,\n" +
                    "        tm.data,\n" +
                    "        tm.dat_finalizacao,\n" +
                    "        tm.id_mov,"
                        + "tm.numero_documento,"
                        + "tm.serie,tm.id_modelo, tm.hora_finalizacao\n" +
                    " from tmovimento tm\n" +
                    " join ttipo_documentos td on tm.id_tipo=td.id\n" +
                    " left join tparceiros tp on tm.id_parceiro=tp.idparceiro\n" +
                    " where tm."+tipodata+" between '"+dataini+"' and '"+datafim+"' and tm.estado=? \n"
                    +"and (tp.razao_social like'%"+campo+"%' or tm.numero_documento like '%"+campo+"%' ) order by 9 desc";
            }
            PreparedStatement ps = conexao.getPreparedStatementResult(sql);
            ps.setString(1, estado);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel tb = (DefaultTableModel) TbLancamentos.getModel();
            TbLancamentos.setRowSorter( new TableRowSorter(tb));
            tb.setNumRows(0);
            while (rs.next()){
                tb.addRow(new Object[]{
                    rs.getString(9),
                    rs.getString(10),
                    rs.getString(1),
                    rs.getString(2),
                    clsaux.formato(rs.getDouble(3)),
                    clsaux.formato(rs.getDouble(4)),
                    clsaux.formato(rs.getDouble(5)),
                    clsaux.formato(rs.getDouble(6)),
                    clsaux.convertDataExib(rs.getString(7)),
                    clsaux.convertDataExib(rs.getString(8)),
                    rs.getString(13),
                    rs.getString(11),
                    rs.getString(12)
                });
                totalreg++;
            }
            rs.close();
            ps.close();
            int cont=tb.getRowCount();
            for(int a=0; a<cont; a++){
                valortotalregistros=valortotalregistros+Double.parseDouble(tb.getValueAt(a, 7).toString().replaceAll(",", "."));
            }
            lbTotalreg.setText(String.valueOf(totalreg));
            lbValorTotal.setText(clsaux.formato(valortotalregistros));
            } catch (SQLException ex) {
            Logger.getLogger(CRUDDocumentos.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao buscar dados \n"+ex.getMessage());
        }
    
    }
