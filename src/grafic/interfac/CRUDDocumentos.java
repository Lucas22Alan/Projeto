    public void deletaMovimento(){
        int yn= JOptionPane.showConfirmDialog(null, "Deseja realmente cancelar o registro selecionado?", "exclusão de registro", JOptionPane.YES_NO_OPTION);
        if (yn== JOptionPane.YES_OPTION){
            String idmov= TbLancamentos.getValueAt(TbLancamentos.getSelectedRow(), 0).toString();
            ClsCancelamentos canc= new ClsCancelamentos();
            canc.cancelarMovimentacaoReferente(idmov);
        }
        this.localizaDocumentos();
    }
