     public void buscaunidade(String campo){
        try {
            String sql="select * from tunidade where nome like '%"+campo+"%';";
            PreparedStatement ps = conexao.getPreparedStatement(sql);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel tb = (DefaultTableModel) tblUnid.getModel();
            tb.setNumRows(0);
            while(rs.next()){
                tb.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                    
                });
            }
            rs.close();
            ps.close();
        } catch (SQLException ex) {
            Logger.getLogger(CRUDTabelas.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
