    public void inicia(){
        txtNome.requestFocus();
        DefaultComboBoxModel cb= new DefaultComboBoxModel(listaDAO.listapagamento().toArray());
        cbFinalizadora.setModel(cb);
    }
    
    public void preparaNovoRegistro(){
        txtId.setText(dao.geraId());
    }
    public void dadosAlterar(String id){
        for(Tcondicoes_venda condicao:dao.buscaDados()){
            if(condicao.getId().equals(id)){
                cond=condicao;
                txtId.setText(cond.getId());
                txtNome.setText(cond.getDescricao());
                txtParcela.setText(cond.getParcelas());
                txtDados.setText(cond.getDados());
                txtTaxa.setText(clsaux.formato(cond.getTaxa()));
                clsaux.carregaComboBox(cbFinalizadora, cond.getFinalizadora());
                txtParceiro.setText(cond.getParceiro());
                if(clsaux.trataCampoNuloConfig(cond.getGera_receber()).equals("S"))ckGeraReceber.setSelected(true); else {
                    ckGeraReceber.setSelected(false);
                }
            }
        }
    }
    public void gravar(){
        cond.setId(txtId.getText());
        cond.setDescricao(txtNome.getText());
        cond.setParcelas(txtParcela.getText());
        cond.setDados(txtDados.getText());
        cond.setTaxa(clsaux.capturaValores(txtTaxa.getText()));
        cond.setFinalizadora(clsaux.retornaId(cbFinalizadora.getSelectedItem()));
        cond.setParceiro(clsaux.trataCampoNulo(txtParceiro.getText()));
        if(ckGeraReceber.isSelected())cond.setGera_receber("S"); else cond.setGera_receber("N");
        dao.inserirAtualizaCondicao(cond);
        this.dispose();
    }
   
    
