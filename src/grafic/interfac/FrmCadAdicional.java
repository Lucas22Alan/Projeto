    public void dadosAlterar(String id){
        cad=new AdicionalDAO().retornaCadAdicional(id);
        txtCodProdBase.setText(cad.getCod_base());
        txtQntUso.setText(clsaux.formato(cad.getQntusar()));
        txtCusto.setText(clsaux.formato(cad.getCusto_base()));
        txtVlVenda.setText(clsaux.formato(cad.getPrec_venda()));
        txtId.setText(cad.getId());
        
        txtDescAdicional.setText(cad.getDescricao());
        txtDescBase.setText(cad.getDescricao_base());
        if(cad.getTipo().equals("1")){
            cbtipo.setSelectedIndex(0);
        }else{
            cbtipo.setSelectedIndex(1);
        }
    }
