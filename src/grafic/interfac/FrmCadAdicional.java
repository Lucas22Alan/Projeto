    public void preencheDados(String codigobarra){
         String sqlitem="select tp.id,\n" +
                        "        tp.nomelongo,\n" +
                        "        tp.unid_venda,\n" +
                        "        tb.codigo_barras,\n" +
                        "        tb.referencia,\n" +
                        "        tc.preco_venda, tc.preco_custo\n" +
                        "        from tprodutos tp\n" +
                        "join tbarras tb on tp.id=tb.id_produto\n" +
                        "join tprecos tc on tp.id=tc.id\n" +
                        "where tb.codigo_barras=? and tp.excluido='N'";
        PreparedStatement psp= conexao.getPreparedStatementResult(sqlitem);
        try {
            psp.setString(1, codigobarra);
            ResultSet rsp= psp.executeQuery();
            rsp.first();
            txtDescBase.setText(rsp.getString(2));
            custoGeral=rsp.getDouble(7);
            txtCusto.setText(clsaux.formato(rsp.getDouble(7)));
            rsp.close();
            psp.close();
        } catch (SQLException ex) {
            Logger.getLogger(LancamentoItemDocumento.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Produto Não Encontrado!!!");
        }
    }
    public void calculaValoritem(){
        Double qnt=clsaux.capturaValores(txtQntUso.getText());
        Double custo=custoGeral*qnt;
        txtCusto.setText(clsaux.formato(custo));
        txtVlVenda.requestFocus();
    }
