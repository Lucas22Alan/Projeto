     private void abrirPesquisaArquivos(){
        int retorno=fileChooser.showOpenDialog(this);
        if (retorno==JFileChooser.APPROVE_OPTION){
            File arquivo=fileChooser.getSelectedFile();
            txtCaminhoArquivo.setText(arquivo.getAbsolutePath());
           
        }else{
            JOptionPane.showMessageDialog(null, "Erro ao Localizar Arquivo!!!");
        }
    }
    
     
     public void lerArquivo() throws IOException, BiffException{
         String caminho=txtCaminhoArquivo.getText();
         
         Workbook book= Workbook.getWorkbook(new File(caminho));
         //pega 1 planilha, se trabalhar com mais de uma so tratar esse sheet
         int tamanho=Integer.parseInt(txtPag.getText());
         for(int l=0; l<tamanho; l++){
         Sheet sheet= book.getSheet(l);
         int tamanhoarquivo=sheet.getRows();
         
         for (int i=0; i<tamanhoarquivo;i++){
             Cell codinterno=sheet.getCell(Integer.parseInt(txtinterno.getText()), i);
             Cell descricao=sheet.getCell(Integer.parseInt(txtdescricao.getText()),i);
             Cell codbarra=sheet.getCell(Integer.parseInt(txtbarras.getText()),i);
             Cell preco=sheet.getCell(Integer.parseInt(txtpreco.getText()),i);
             Cell codncm=sheet.getCell(Integer.parseInt(txtncm.getText()),i);
             Cell codcusto=sheet.getCell(Integer.parseInt(txtcusto.getText()),i);
             Cell codgrupo=sheet.getCell(Integer.parseInt(txtgrupo.getText()),i);
             Cell codunid=sheet.getCell(Integer.parseInt(txtunidade.getText()),i);
             Cell codmargem=sheet.getCell(Integer.parseInt(txtmargem.getText()),i);
             Cell codcst=sheet.getCell(Integer.parseInt(txtcst.getText()),i);
             
             String precoconv=preco.getContents().replaceAll("R", "");
             precoconv=precoconv.replaceAll("\\$", "");
             precoconv=precoconv.replaceAll(" ", "");
             String cust=codcusto.getContents().replaceAll("R", "");
             cust=cust.replaceAll("\\$", "");
             cust=cust.replaceAll(" ", "");
             criaProdutoBase(codinterno.getContents(),
                            descricao.getContents(),
                            validaDigitosBarras.preencheCodigo(codbarra.getContents()),
                            clsaux.capturaValor(precoconv),
                            codncm.getContents(),
                            clsaux.capturaValor(cust),
                            codgrupo.getContents(),
                            codunid.getContents(),
                             clsaux.capturaValor(codmargem.getContents()),
                             codcst.getContents()
                            );
         }
         }
        JOptionPane.showMessageDialog(null, "Importação Finalizada!!!\nLinhas Processadas ");
        }
