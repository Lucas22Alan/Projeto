    public void atalhos(){
        InputMap imp= this.getRootPane().getInputMap(JPanel.WHEN_IN_FOCUSED_WINDOW);
        imp.put(KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE,0),"sair");
        this.getRootPane().setInputMap(JPanel.WHEN_IN_FOCUSED_WINDOW, imp);
        this.getRootPane().getActionMap().put("sair", new AbstractAction(){
            public void actionPerformed(ActionEvent ev){
              dispose();
            }
        });
        
        tbl.getInputMap(tbl.WHEN_ANCESTOR_OF_FOCUSED_COMPONENT).put(KeyStroke.getKeyStroke("ENTER"), "enterAction");
        tbl.getActionMap().put("enterAction", new AbstractAction(){
            public void actionPerformed(ActionEvent e){
               confirmaInf();
            }
        });
        
        tbl.addMouseListener(new MouseAdapter(){
            public void mouseClicked(MouseEvent e ){
                if (e.getClickCount()==2){
                   confirmaInf();
                } 
            }
          
        }); 
    }
    public void inicia(String barra){
        compras= new BaseDevolucaoDAO().retornaListaMovimentoRefItem(barra);
        carregaTabela();
    }
    
    public void carregaTabela(){
        DefaultTableModel tb = (DefaultTableModel) tbl.getModel();
        tb.setNumRows(0);
        for(ClsDadosDevolucaoItem comp:compras){
            tb.addRow(new Object[]{
                comp.getData(),
                comp.getDocumento(),
                comp.getSerie(),
                comp.getQuantidade(),
                comp.getPreco()
            });
        }
    }
    
    public void confirmaInf(){
        frmLancamentoNfe.dadosDev=compras.get(tbl.getSelectedRow());
        dispose();
    }
