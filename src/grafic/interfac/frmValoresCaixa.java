    public void pesquisaCaixa(){
        pnEstado.removeAll();
        try {
            Date dtini, dtfim=null;
            String cx = null,idfinalizadora=null,numpdv=null;
            Double venda=0.00,retirada = 0.00,saldo = 0.00,din = 0.00,cred=0.00,deb=0.00,suprimento=0.00;
            dtini=clsaux.retornaData(ftInicio.getText());
            dtfim=clsaux.retornaData(ftFim.getText());
            String sql="select tp.id_caixa,sum(tm.total),tp.id_pdv from tcaixa_pdv tp\n" +
                    "join tmovimento tm on tp.id_caixa=tm.id_caixa\n" +
                    "where status='A' and data_abertura between '"+dtini+"' and '"+dtfim+"' and tm.estado='2'\n" +
                    "group by 1,3";
            PreparedStatement ps = conexao.getPreparedStatementResult(sql);
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                venda=rs.getDouble(2);
                cx=rs.getString(1);
                numpdv=rs.getString(3);
                String sqlvl="select  sum(tp.valor-tp.troco) as total,\n" +
                            "        tf.descricao,\n" +
                            "        tp.id_finalizadora,\n" +
                            "        (select sum(tv.valor_saida)from tmov_caixa tv where tv.id_caixa="+cx+") as saida,\n" +
                            "        (select sum(tv.valor_entrada)from tmov_caixa tv where tv.id_caixa="+cx+") as entrada\n" +
                            "    from tmovimento tm\n" +
                            "    left join tpagamentos_pdv tp on tm.id_mov=tp.id_movimento\n" +
                            "    left join tfinalizadora tf on tp.id_finalizadora=tf.id_finalizdora\n" +
                            "    where tm.id_caixa='"+cx+"' and tp.estado=2 and tm.estado='2'\n" +
                            "    group by 2,3";
                PreparedStatement ps1= conexao.getPreparedStatementResult(sqlvl);
                ResultSet rs1= ps1.executeQuery();
                while(rs1.next()){
                    idfinalizadora=rs1.getString(3);
                    if(idfinalizadora.equals("1")){
                        din=rs1.getDouble(1);
                        retirada=rs1.getDouble(4);
                        suprimento=rs1.getDouble(5);
                        saldo=din+suprimento-retirada;
                    }else if(idfinalizadora.equals("2")){
                        cred=rs1.getDouble(1);
                    
                    }else if(idfinalizadora.equals("1")){
                        deb=rs1.getDouble(1);
                    }
                
                }
            this.criaLabel(retirada,saldo,din,cred,deb,venda,suprimento,numpdv);
            
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmValoresCaixa.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    
    
    }
