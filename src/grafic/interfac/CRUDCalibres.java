    public void carregaDadosAlterar(){
        tipos=dao.buscaTipos(tbCalibre.getValueAt(tbCalibre.getSelectedRow(), 0).toString());
        carregaTabelaTipos();
        jTabbedPane1.setSelectedIndex(1);
    }
   
    
    public void preparaIncluirNovo(){
         DefaultTableModel tb= (DefaultTableModel) tbCalibre.getModel();
         calibre= new TcadCalibre();
         calibre.setId( new BaseGeralDAO().gerarIds("GEN_TCAD_CALIBRE_ID"));
         new criaBotaoTabelaCalibres(tbCalibre, 2,"Gravar");
         tb.addRow( new Object[]{
             calibre.getId(),
             "",
             ""
         });
    } 
    
    public void gravarTipo(){
        TcadCalibreTiro tipo= new TcadCalibreTiro();
        tipo.setId(new BaseGeralDAO().gerarIds("GEN_CAD_GERAL"));
        tipo.setIdtipo(tbCalibre.getValueAt(tbCalibre.getSelectedRow(), 0).toString());
        tipo.setNome(txtNome.getText());
         if(dao.inserirTipoCalibre(tipo)){
              tipos.add(tipo);
              carregaTabelaTipos();
              txtNome.setText("");
         }
    }
  
    public void buscaDados(){
        calibres= dao.buscaCalibres();
      carregaTabela();
    }
    
    
     public void carregaTabelaTipos(){
        DefaultTableModel tb= (DefaultTableModel) tbDados.getModel();
        tb.setNumRows(0);
        tipos.forEach((cam) -> {
            tb.addRow(new Object[]{
                cam.getId(),
                cam.getNome()
            });
        });
    }
    
    public void carregaTabela(){
        DefaultTableModel tb= (DefaultTableModel) tbCalibre.getModel();
        tb.setNumRows(0);
        new criaBotaoTabelaCalibres(tbCalibre, 2,"Gravar");
        calibres.forEach((cam) -> {
            tb.addRow(new Object[]{
                cam.getId(),
                cam.getNome(),
                ""
            });
        });
    }
