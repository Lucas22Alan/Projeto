    public void inicia(){
        DefaultComboBoxModel JcbCont=new DefaultComboBoxModel(MovimentoContaDAO.listaconta().toArray());
        DefaultComboBoxModel cndest=new DefaultComboBoxModel(MovimentoContaDAO.listaconta().toArray());
        cbContaOrigem.setModel(JcbCont);
        cbContaDestino.setModel(cndest);
        txtDataMovimento.setText(clsaux.preencheData());
    }
    
    public void gravar(){
       if(clsaux.capturaValores(txtValor.getText())>0.00){
           this.gerarSaida();
           this.gerarEntrada();
           this.dispose();
       }
       
    }
    public void gerarSaida(){
        CadDocMovConta cadmc = new CadDocMovConta();
        MovimentoContaDAO mvDAO= new MovimentoContaDAO();
        cadmc.setIdconta(clsaux.retornaId(cbContaOrigem.getSelectedItem().toString()));
        cadmc.setIdparceiro("0");
        cadmc.setN_doc("000");
        cadmc.setData_lancamento(clsaux.retornaData(txtDataMovimento.getText()));
        cadmc.setTipo_mov("1000");
        cadmc.setSubtipo_mov("1002");
        cadmc.setEntrada("0.00");
        cadmc.setSaida(clsaux.capturaValor(txtValor.getText()));
        cadmc.setData_venc("");
        cadmc.setIdfinalizadora("1");
        cadmc.setObservacao(JtxpObs.getText());
        cadmc.setIddocumento("");
        cadmc.setDesc_mov("Transferencia De Valores Entre Contas, Conta Destino "+cbContaDestino.getSelectedItem().toString());
        mvDAO.inserir(cadmc);
    }
    public void gerarEntrada(){
        CadDocMovConta cadmc = new CadDocMovConta();
        MovimentoContaDAO mvDAO= new MovimentoContaDAO();
        cadmc.setIdconta(clsaux.retornaId(cbContaDestino.getSelectedItem().toString()));
        cadmc.setIdparceiro("0");
        cadmc.setN_doc("000");
        cadmc.setData_lancamento(clsaux.retornaData(txtDataMovimento.getText()));
        cadmc.setTipo_mov("1000");
        cadmc.setSubtipo_mov("1001");
        cadmc.setEntrada(clsaux.capturaValor(txtValor.getText()));
        cadmc.setSaida("0.00");
        cadmc.setData_venc("");
        cadmc.setIdfinalizadora("1");
        cadmc.setObservacao(JtxpObs.getText());
        cadmc.setIddocumento("");
        cadmc.setDesc_mov("Transferencia De Valores Entre Contas, Conta origem "+cbContaOrigem.getSelectedItem().toString());
        mvDAO.inserir(cadmc);
    
    }
