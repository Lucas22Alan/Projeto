    public void inicia(String cpfAtirador){
        TarmasDAO dao = new TarmasDAO();
         DefaultComboBoxModel cb= new DefaultComboBoxModel(dao.retornaListaArmas(cpfAtirador).toArray());
         cbRegistro.setModel(cb);
        
    }
    
    public void preencheCampos(){
         Tcad_armas arma= (Tcad_armas) cbRegistro.getSelectedItem();
         txtCalibre.setText(arma.getModelo());
         txtEspecie.setText(arma.getEspecie());
         txtModelo.setText(arma.getModelo());
         
    }
    
    public void confirmaDados(){
       if(clsaux.capturaValores(txtQnt.getText())>0.00){
           int quantidade = Integer.parseInt(txtQnt.getText());
           Tcad_armas arma=(Tcad_armas)cbRegistro.getSelectedItem();
           arma.setQnt(quantidade);
           frmIncluiRegHab.armas.add(arma);
           this.dispose();
       }
       else{
           JOptionPane.showMessageDialog(null, "Quantidade Invalida");
       }
    
    }
