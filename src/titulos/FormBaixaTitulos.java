    public void recalculaTabela(){
        Double juro=clsaux.capturaValores(txtJuros.getText());
        Double multa=clsaux.capturaValores(txtMulta.getText());
        Double vl=0.00;
        int titulos=jTable2.getRowCount();
        juro=juro/titulos;
        multa=multa/titulos;
        for(int i=0; i<titulos; i++){
            vl= clsaux.capturaValores(jTable2.getValueAt(i, 3).toString());
            vl=vl+juro+multa;
            jTable2.setValueAt(juro, i, 4);
            jTable2.setValueAt(multa, i, 5);
            jTable2.setValueAt(vl, i, 7);
            jTable2.setValueAt(vl, i, 8);
        }
    }
