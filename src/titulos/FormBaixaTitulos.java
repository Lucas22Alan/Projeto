    public void divideValorTitulos(){
        Double valordigitado=0.00;
        int linha=tblValores.getRowCount();
        for (int i=0; i<linha;i++){
            valordigitado=valordigitado+Double.parseDouble(tblValores.getValueAt(i, 2).toString().replaceAll(",", "."));
            }
        linha= jTable2.getRowCount();
        Double perc=valordigitado*100/clsaux.capturaValores(btnTotal.getText());
        DefaultTableModel tbl = (DefaultTableModel) jTable2.getModel();
        for (int l=0; l<linha; l++){
            Double vltitulo=0.00;
            vltitulo=Double.parseDouble(jTable2.getValueAt(l, 7).toString().replaceAll(",", "."));
            vltitulo=vltitulo*perc/100.0;
            vltitulo=clsaux.capturaValores(clsaux.formato(vltitulo));
            if(valordigitado>=vltitulo){
                 jTable2.setValueAt(vltitulo, l, 8);
                 valordigitado=clsaux.capturaValores(clsaux.formato(valordigitado-vltitulo));
                
            }else{
                 jTable2.setValueAt(valordigitado, l, 8);
            }
        }
    }
