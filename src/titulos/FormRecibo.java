    public void acaoImprimir(){
        try {
            String nome,valor,referencia=null;
            nome=txtNome.getText();
            valor=txtValor.getText();
            referencia=txtReferencia.getText();
            Double valorconv=Double.parseDouble(valor.replaceAll(",","."));
            System.out.println(valorconv);
            String valorext=getNumPorExtenso(valorconv);
            System.out.println(valorext);
            JDialog visu= new JDialog(new javax.swing.JFrame(),"Visualizacao de Recibo",true);
            visu.setSize(1040,720);
            visu.setLocationRelativeTo(null);
            JasperDesign jd;
            jd=JRXmlLoader.load(".\\relatorios\\Recibopgto.jrxml");
            JasperReport js= JasperCompileManager.compileReport(jd);
            Map param= new HashMap();
            param.put("pValor", valor);
            param.put("pNome", nome);
            param.put("pValorExtenso",valorext);
            param.put("pReferencia",referencia);
            param.put("pData",clsaux.preencheData());
            JasperPrint jp=JasperFillManager.fillReport(js, param);
            JasperViewer jw= new JasperViewer(jp,true);
            visu.getContentPane().add(jw.getContentPane());
            visu.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(FormRecibo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
